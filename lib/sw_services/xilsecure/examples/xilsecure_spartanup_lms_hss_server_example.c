/*****************************************************************************/
/**
*
* This function initializes the PMC DMA and SHA instances.
*
* @param	PmcDma	Pointer to PmcDma instance to initialize.
* @param	ShaInst	Pointer to SHA instance to initialize.
*
* @return
*		- XST_SUCCESS - on successful initialization.
*		- XST_FAILURE - if DMA config lookup, DMA init, or SHA init fails.
*
****************************************************************************/
static int XSecure_InitDmaAndSha(XPmcDma *PmcDma, XSecure_Sha *ShaInst)
{
	int Status = XST_FAILURE;
	XPmcDma_Config *Config;

	Config = XPmcDma_LookupConfig(0U);
	if (Config == NULL) {
		xil_printf("PmcDma config lookup failed\n\r");
		goto END;
	}

	Status = XPmcDma_CfgInitialize(PmcDma, Config, Config->BaseAddress);
	if (Status != XST_SUCCESS) {
		xil_printf("PmcDma init failed\n\r");
		goto END;
	}

	Status = XSecure_ShaInitialize(ShaInst, PmcDma);
	if (Status != XST_SUCCESS) {
		xil_printf("SHA init failed, Status = 0x%x\n\r", Status);
		goto END;
	}

END:
	return Status;
}

/*****************************************************************************/
/**
*
* Main function to call the LmsShakeVerifyExample and HssShakeVerifyExample
*
* @return
*		- XST_FAILURE if the LMS or HSS SHAKE256 signature verification failed.
*
******************************************************************************/
 int main(void)
 {
	 int Status = XST_FAILURE;

	 Status = XSecure_LmsShakeVerifyExample();
	 if (Status != XST_SUCCESS) {
		 xil_printf("LMS/HSS SHAKE example failed at LMS, Status=0x%x\n\r", Status);
		 goto END;
	 }

	 Status = XSecure_HssShakeVerifyExample();
	 if (Status != XST_SUCCESS) {
		 xil_printf("LMS/HSS SHAKE example failed at HSS, Status=0x%x\n\r", Status);
		 goto END;
	 }

	 xil_printf("Successfully ran LMS and HSS SHAKE256 signature verification example\n\r");

 END:
	 return Status;
 }

/****************************************************************************
*
* This function verifies the LMS SHAKE256 signature.
*
* @return
*		- XST_SUCCESS - if the LMS SHAKE256 signature verification passed.
*		- XST_FAILURE - if the LMS SHAKE256 signature verification failed.
*
****************************************************************************/
static int XSecure_LmsShakeVerifyExample(void)
{
	int Status = XST_FAILURE;
	XSecure_LmsSignVerifyParams LmsParams;

	Status = Xil_SMemSet(&SecureSha, sizeof(SecureSha), 0U, sizeof(SecureSha));
	if (Status != XST_SUCCESS) {
		goto END;
	}

	Status = XSecure_InitDmaAndSha(&PmcDma, &SecureSha);
	if (Status != XST_SUCCESS) {
		xil_printf("LMS: DMA/SHA init failed, Status = 0x%x\n\r", Status);
		goto END;
	}

	LmsParams.Data = (u8 *)LmsShake256Data;
	LmsParams.DataLen = XSECURE_LMS_HSS_EXAMPLE_DATA_SIZE;
	LmsParams.PreHashedMsg = FALSE;
	LmsParams.LmsSign = (u8 *)LmsShake256Signature;
	LmsParams.LmsSignLen = XSECURE_LMS_HSS_EXAMPLE_LMS_SIG_SIZE;
	LmsParams.ExpectedPubKey = (u8 *)LmsShake256PubKey;
	LmsParams.PubKeyLen = XSECURE_LMS_PUB_KEY_TOTAL_SIZE;

	Status = XSecure_LmsSignatureVerification(&SecureSha, &PmcDma, &LmsParams);
	if (Status != XST_SUCCESS) {
		xil_printf("LMS signature verification failed, Status = 0x%x\n\r", Status);
		goto END;
	}

	xil_printf("LMS SHAKE256 signature verification passed\n\r");

END:
	return Status;
}

/****************************************************************************
*
* This function verifies the HSS SHAKE256 signature.
*
* @return
*		- XST_SUCCESS - if the HSS SHAKE256 signature verification passed.
*		- XST_FAILURE - if the HSS SHAKE256 signature verification failed.
*
****************************************************************************/
static int XSecure_HssShakeVerifyExample(void)
{
	int Status = XST_FAILURE;
	XSecure_HssInitParams HssParams;

	Status = Xil_SMemSet(&SecureSha, sizeof(SecureSha), 0U, sizeof(SecureSha));
	if (Status != XST_SUCCESS) {
		goto END;
	}

	Status = XSecure_InitDmaAndSha(&PmcDma, &SecureSha);
	if (Status != XST_SUCCESS) {
		xil_printf("HSS: DMA/SHA init failed, Status = 0x%x\n\r", Status);
		goto END;
	}

	HssParams.SignBuff = (u8 *)HssShake256Signature;
	HssParams.SignatureLen = XSECURE_LMS_HSS_EXAMPLE_HSS_SIG_SIZE;
	HssParams.PublicKey = (u8 *)HssShake256PubKey;
	HssParams.PublicKeyLen = XSECURE_HSS_PUBLIC_KEY_TOTAL_SIZE;

	Status = XSecure_HssSignatureVerification(&SecureSha, &PmcDma, &HssParams,
				(u8 *)HssShake256Data, XSECURE_LMS_HSS_EXAMPLE_DATA_SIZE);
	if (Status != XST_SUCCESS) {
		xil_printf("HSS signature verification failed, Status = 0x%x\n\r", Status);
		goto END;
	}

	xil_printf("HSS SHAKE256 signature verification passed\n\r");

END:
	return Status;
}

