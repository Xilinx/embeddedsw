/*****************************************************************************/
/**
*
* Main function to call the MLDSA client example
*
* @return
*		- XST_SUCCESS if example runs successfully
*		- Error code if the example fails.
*
******************************************************************************/
int main(void)
{
	int Status = XST_FAILURE;
	XMailbox MailboxInstance;
	XSecure_ClientInstance SecureClientInstance;
	u8 *GeneratedSignature = NULL;
	u32 MessageLen;

	#ifdef XSECURE_CACHE_DISABLE
		Xil_DCacheDisable();
	#endif

	xil_printf("MLDSA Client Example\r\n");

	Status = XMailbox_Initialize(&MailboxInstance, 0U);
	if (Status != XST_SUCCESS) {
		xil_printf("Mailbox initialize failed: Status = %08x \r\n", Status);
		goto END;
	}

	Status = XSecure_ClientInit(&SecureClientInstance, &MailboxInstance);
	if (Status != XST_SUCCESS) {
		xil_printf("Client initialize failed: Status = %08x \r\n", Status);
		goto END;
	}

	/* Set shared memory */
	Status = XMailbox_SetSharedMem(&MailboxInstance,
		(u64)(UINTPTR)(SharedMem + XSECURE_MLDSA_SIGN_LEN),
		XSECURE_SHARED_MEM_SIZE);
	if (Status != XST_SUCCESS) {
		xil_printf("Shared memory initialization failed: Status = %08x \r\n", Status);
		goto END;
	}

	Status = XSecure_SetSlrIndex(&SecureClientInstance, SLR_INDEX);
	if (Status != XST_SUCCESS) {
		xil_printf("Invalid SlrIndex: Status = %08x \r\n", Status);
		goto END;
	}

	/* Use shared memory for generated signature */
	GeneratedSignature = &SharedMem[0U];
	MessageLen = sizeof(TestMessage) - 1U; /* Exclude null terminator */

	xil_printf("\r\n--- MLDSA Signature Generation Test ---\r\n");
	xil_printf("Message: %s\r\n", TestMessage);
	xil_printf("Message Length: %u bytes\r\n", MessageLen);
	xil_printf("Secret Key Length: %u bytes\r\n", XSECURE_MLDSA_SK_LEN);

	/* Flush cache for input data */
	Xil_DCacheFlushRange((UINTPTR)TestMessage, MessageLen);
	Xil_DCacheFlushRange((UINTPTR)mldsa87_prv_key, XSECURE_MLDSA_SK_LEN);
	Xil_DCacheInvalidateRange((UINTPTR)GeneratedSignature, XSECURE_MLDSA_SIGN_LEN);

	/* Fill signature generation parameters */
	SignGenParams.DataAddr = (UINTPTR)TestMessage;
	SignGenParams.DataLen = MessageLen;
	SignGenParams.SecretKeyAddr = (UINTPTR)mldsa87_prv_key;
	SignGenParams.SecretKeyLen = XSECURE_MLDSA_SK_LEN;
	SignGenParams.SignatureAddr = (UINTPTR)GeneratedSignature;
	SignGenParams.SignatureLen = XSECURE_MLDSA_SIGN_LEN;

	Xil_DCacheFlushRange((UINTPTR)&SignGenParams, sizeof(SignGenParams));

	/* Generate signature */
	Status = XSecure_MldsaSignGenerateClient(&SecureClientInstance,
		(UINTPTR)&SignGenParams);
	if (Status != XST_SUCCESS) {
		xil_printf("MLDSA signature generation failed: Status = %08x\r\n", Status);
		goto END;
	}

	Xil_DCacheInvalidateRange((UINTPTR)GeneratedSignature, XSECURE_MLDSA_SIGN_LEN);

	xil_printf("MLDSA signature generated successfully\r\n");
	xil_printf("Signature length: %u bytes\r\n", XSECURE_MLDSA_SIGN_LEN);

	/* Print first 64 bytes of signature */
	xil_printf("First 64 bytes of signature:\r\n");
	XSecure_ShowData(GeneratedSignature, 64U);

	xil_printf("\r\n--- MLDSA Signature Verification Test ---\r\n");

	/* Flush cache for verification inputs */
	Xil_DCacheFlushRange((UINTPTR)mldsa87_pub_key, XSECURE_MLDSA_PK_LEN);
	Xil_DCacheFlushRange((UINTPTR)GeneratedSignature, XSECURE_MLDSA_SIGN_LEN);

	/* Fill signature verification parameters */
	SignVerifyParams.DataAddr = (UINTPTR)TestMessage;
	SignVerifyParams.DataLen = MessageLen;
	SignVerifyParams.PubKeyAddr = (UINTPTR)mldsa87_pub_key;
	SignVerifyParams.PubKeyLen = XSECURE_MLDSA_PK_LEN;
	SignVerifyParams.SignAddr = (UINTPTR)GeneratedSignature;
	SignVerifyParams.SignLen = XSECURE_MLDSA_SIGN_LEN;
	SignVerifyParams.ContextAddr = 0U;
	SignVerifyParams.ContextLen = 0U;

	Xil_DCacheFlushRange((UINTPTR)&SignVerifyParams, sizeof(SignVerifyParams));

	/* Verify signature */
	Status = XSecure_MldsaSignVerifyClient(&SecureClientInstance,
		(UINTPTR)&SignVerifyParams);
	if (Status != XST_SUCCESS) {
		xil_printf("MLDSA signature verification failed: Status = %08x\r\n", Status);
		goto END;
	}

	xil_printf("MLDSA signature verified successfully\r\n");
	xil_printf("\r\n--- MLDSA Test Complete ---\r\n");

END:
	Status |= XMailbox_ReleaseSharedMem(&MailboxInstance);
	if (Status != XST_SUCCESS) {
		xil_printf("MLDSA example failed with Status: %08x\r\n", Status);
	}
	else {
		xil_printf("Successfully ran MLDSA client example\r\n");
	}

	return Status;
}

/*****************************************************************************/
/**
*
* This function displays Data of specified length in hexadecimal format.
*
* @param 	Data 	Pointer to the data to be displayed
* @param	Len	Length of the data to be displayed
*
******************************************************************************/
static void XSecure_ShowData(const u8* Data, u32 Len)
{
	u32 Index;

	for (Index = 0U; Index < Len; Index++) {
		xil_printf("%02x", Data[Index]);
		if (((Index + 1U) % 16U) == 0U) {
			xil_printf("\r\n");
		}
		else if (((Index + 1U) % 8U) == 0U) {
			xil_printf(" ");
		}
	}
	xil_printf("\r\n");
}
