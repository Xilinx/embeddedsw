/*************************************************************************************************/
/**
 * @brief	Callback handler for LMS client KAT operations. Sets the completion flag and
 *		captures the operation status via per-invocation state.
 *
 * @param	CallBackRef	Pointer to the caller's XAsu_LmsKatCbState on stack.
 * @param	Status		Operation status returned by ASUFW.
 *
 *************************************************************************************************/
static void XAsu_LmsKatCallBack(void *CallBackRef, u32 Status)
{
	XAsu_LmsKatCbState *State = (XAsu_LmsKatCbState *)CallBackRef;

	State->CallBackStatus = (s32)Status;
	State->Notify = 1U;
}

/*************************************************************************************************/
/**
 * @brief	This function runs LMS signature verification KAT from the client side.
 *		It sends an LMS sign verify request to ASUFW using known good test vectors,
 *		busy-waits for the response, and returns the verification result.
 *
 * @return
 *		- XST_SUCCESS, if LMS signature verification KAT passes.
 *		- XST_FAILURE, if the KAT fails.
 *
 *************************************************************************************************/
s32 XAsu_LmsSignVerifyKat(void)
{
	s32 Status = XST_FAILURE;
	XAsu_LmsHssSignVerifyParams LmsParams = {0U};
	XAsu_ClientParams ClientParams = {0U};
	XAsu_LmsKatCbState KatState = {0U, (s32)XST_FAILURE};

	/** Configure LMS signature verification parameters using KAT vectors. */
	LmsParams.MsgAddr = (u64)(UINTPTR)LmsKatMessage;
	LmsParams.SignatureAddr = (u64)(UINTPTR)LmsKatSignature;
	LmsParams.LmsHssKeyObj.PubKeyAddr = (u64)(UINTPTR)LmsKatPublicKey;
	LmsParams.MsgLen = XASU_LMS_KAT_MSG_SIZE;
	LmsParams.SignatureLen = XASU_LMS_KAT_SIG_SIZE;
	LmsParams.LmsHssKeyObj.PubKeyLen = XASU_LMS_KAT_PUB_KEY_SIZE;
	LmsParams.PreHashedMsg = XASU_LMS_MSG_NOT_PREHASHED;
	LmsParams.ShaType = XASU_SHA2_TYPE;
	LmsParams.ShaMode = XASU_SHA_MODE_256;
	LmsParams.LmsHssKeyObj.PubKeyId = 0U;

	/** Configure client parameters for synchronous operation. */
	ClientParams.Priority = XASU_PRIORITY_HIGH;
	ClientParams.SecureFlag = XASU_CMD_SECURE;
	ClientParams.CallBackFuncPtr = (XAsuClient_ResponseHandler)((void *)XAsu_LmsKatCallBack);
	ClientParams.CallBackRefPtr = (void *)&KatState;

	/** Send LMS signature verification request to ASUFW. */
	Status = XAsu_LmsSignVerify(&ClientParams, &LmsParams);
	if (Status != XST_SUCCESS) {
		goto END;
	}

	/** Busy-wait for ASUFW response. */
	while (KatState.Notify == 0U) {
		/* Wait */
	}

	/** Check verification status from callback. */
	Status = KatState.CallBackStatus;

END:
	return Status;
}

