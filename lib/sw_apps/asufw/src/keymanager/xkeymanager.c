/**
 * @brief	Get the number of active RSA private keys in the ASU internal vault.
 *
 * @param	KeyType	Identifier of the RSA key type to query.
 *
 * @return	Number of active RSA private keys, or 0 if the vault is not found.
 *
 *************************************************************************************************/
u16 XKeyManager_GetAsuRsaActiveKeyCount(XAsu_KeyManagerSubVaultType KeyType)
{
	XKeyManager *KeyVaultPtr;
	u16 ActiveKeys = 0U;

	if(KeyType >= XKEYMANAGER_MAX_SUB_VAULTS) {
		goto END;
	}

	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[XKEYMANAGER_ASU_VAULT_ID].VaultBasePtr;
	if (KeyVaultPtr != NULL) {
		ActiveKeys = KeyVaultPtr->SubVaultHeaders[KeyType].ActiveKeys;
	}

END:
	return ActiveKeys;
}

/*************************************************************************************************/
/**
 * @brief	Fetch a pre-generated RSA key pair from the ASU internal vault and remove it.
 *			Copies the private key content to the destination buffer and deletes both the
 *			private and public keys from the vault.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	DestBuf		Pointer to the destination buffer.
 * @param	VaultId		Identifier of the vault to fetch the key from.
 * @param	KeyAddr		Destination address for DMA transfer (0 if vault storage).
 *
 * @return
 *	- XASUFW_SUCCESS, if key pair is fetched and removed successfully.
 *	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameter is invalid.
 *	- XASUFW_KEYMANAGER_KEY_NOT_FOUND, if no active RSA key is available in the vault.
 *	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *
 *************************************************************************************************/
s32 XKeyManager_FetchRsaKeyPairFromAsuVault(XAsufw_Dma *DmaPtr, u8 *DestBuf, u8 VaultId, u64 KeyAddr)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XKeyManager *KeyVaultPtr = NULL;
	XAsu_KeyManagerKeyMetadata *KeyMetaPtr = NULL;
	XAsu_RsaKeyPairObject *SrcKeyPtr = NULL;
	XAsu_KeyManagerKeyMetadata *PvtKeyMetaPtr = NULL;
	u32 KeyObjSize;
	u32 CompositeKeyIds[XKEYMANAGER_ASYM_KEY_TYPE_COUNT] = {0U};
	u32 KeyLen;
	u8 *SubVaultBase;
	u64 DestAddr;
	u16 Capacity;
	u16 Index;
	XAsu_KeyManagerSubVaultType KeyType;
	u32 TypeIdx;

	/** Validate input parameters. */
	if ((DestBuf == NULL) || (DmaPtr == NULL)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	if (VaultId != XKEYMANAGER_ASU_VAULT_ID) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;
	if (KeyVaultPtr == NULL) {
		Status = XASUFW_FAILURE;
		goto END;
	}

	/** If no active RSA keys are available, error out. */
	if ((KeyVaultPtr->SubVaultHeaders[XASU_RSA_PVT_SUBVAULT_ID].ActiveKeys == 0U) ||
	    (KeyVaultPtr->SubVaultHeaders[XASU_RSA_PUB_SUBVAULT_ID].ActiveKeys == 0U)) {
		Status = XASUFW_KEYMANAGER_KEY_NOT_FOUND;
		goto END;
	}

	/** Phase 1: Find the first active key in each sub-vault and record composite IDs. */
	for (TypeIdx = 0U; TypeIdx < XKEYMANAGER_ASYM_KEY_TYPE_COUNT; TypeIdx++) {
		KeyType = (XAsu_KeyManagerSubVaultType)((TypeIdx == 0U) ? XASU_RSA_PVT_SUBVAULT_ID :
					    XASU_RSA_PUB_SUBVAULT_ID);
		KeyObjSize = XKeyManager_KeyObjectSizeLookup[KeyType];
		Capacity = KeyVaultPtr->SubVaultHeaders[KeyType].Capacity;
		SubVaultBase = (u8 *)KeyVaultPtr +
			       KeyVaultPtr->SubVaultHeaders[KeyType].Offset;

		for (Index = 0U; Index < Capacity; Index++) {
			KeyMetaPtr = (XAsu_KeyManagerKeyMetadata *)(SubVaultBase +
					(KeyObjSize * (u32)Index));
			if (KeyMetaPtr->KeyId != 0U) {
				CompositeKeyIds[TypeIdx] = (u32)KeyMetaPtr->KeyId |
					((u32)KeyType << XKEYMANAGER_KEY_TYPE_SHIFT_VAL) |
					((u32)VaultId <<
					 XKEYMANAGER_VAULT_ID_SHIFT_VAL);
				if (KeyType == XASU_RSA_PVT_SUBVAULT_ID) {
					PvtKeyMetaPtr = KeyMetaPtr;
				}
				break;
			}
		}
		if (Index == Capacity) {
			Status = XASUFW_KEYMANAGER_KEY_NOT_FOUND;
			goto END;
		}
	}

	/** Phase 2: Copy private key content to the destination. */
	if (KeyAddr != 0U) {
		SrcKeyPtr = (XAsu_RsaKeyPairObject *)((u8 *)PvtKeyMetaPtr +
				sizeof(XAsu_KeyManagerKeyMetadata));
		KeyLen = PvtKeyMetaPtr->Length;
		DestAddr = KeyAddr;

		/** Copy Modulus via DMA. */
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->Modulus,
				       DestAddr, KeyLen, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
		DestAddr += KeyLen;

		/** Copy PvtExp via DMA. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->PvtExp,
				       DestAddr, KeyLen, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
		DestAddr += KeyLen;

		/** Copy Prime1 via DMA. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->Prime1,
				       DestAddr, XKEYMANAGER_HALF_KEY_LEN(KeyLen), 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
		DestAddr += XKEYMANAGER_HALF_KEY_LEN(KeyLen);

		/** Copy Prime2 via DMA. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->Prime2,
				       DestAddr, XKEYMANAGER_HALF_KEY_LEN(KeyLen), 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
		DestAddr += XKEYMANAGER_HALF_KEY_LEN(KeyLen);

		/** Copy DP via DMA. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->DP,
				       DestAddr, XKEYMANAGER_HALF_KEY_LEN(KeyLen), 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
		DestAddr += XKEYMANAGER_HALF_KEY_LEN(KeyLen);

		/** Copy DQ via DMA. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->DQ,
				       DestAddr, XKEYMANAGER_HALF_KEY_LEN(KeyLen), 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
		DestAddr += XKEYMANAGER_HALF_KEY_LEN(KeyLen);

		/** Copy QInv via DMA. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       (u64)(UINTPTR)SrcKeyPtr->QInv,
				       DestAddr, XKEYMANAGER_HALF_KEY_LEN(KeyLen), 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END;
		}
	} else {
		/**
		 * For vault storage, copy the entire max-size
		 * structure to DestBuf.
		 */
		Status = Xil_SMemCpy(DestBuf,
			XRSA_MAX_KEY_OBJ_SIZE_IN_BYTES,
			(u8 *)PvtKeyMetaPtr + sizeof(XAsu_KeyManagerKeyMetadata),
			XRSA_MAX_KEY_OBJ_SIZE_IN_BYTES,
			XRSA_MAX_KEY_OBJ_SIZE_IN_BYTES);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_MEM_COPY_FAIL;
			goto END;
		}
	}

	/** On successful copy, delete both keys from the vault. */
	for (TypeIdx = 0U; TypeIdx < XKEYMANAGER_ASYM_KEY_TYPE_COUNT; TypeIdx++) {
		if (CompositeKeyIds[TypeIdx] != 0U) {
			ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
			Status = XKeyManager_DeleteKey(CompositeKeyIds[TypeIdx],
						XKEYMANAGER_ASU_SUBSYSTEM_ID);
			if (Status != XASUFW_SUCCESS) {
				Status = XAsufw_UpdateErrorStatus(Status,
						XASUFW_KEYMANAGER_KEY_DELETE_ERROR);
				goto END;
			}
		}
	}

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Store a pre-generated RSA key pair into the ASU internal vault.
 *
 * @param	KeyData		Pointer to the key data buffer.
 * @param	KeyLen		Length of the RSA key in bytes.
 * @param	VaultId		Identifier of the vault to store the key in.
 *
 * @return
 *	- XASUFW_SUCCESS, if key pair is stored successfully.
 *	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameter is invalid.
 *	- XASUFW_KEYMANAGER_UPDATE_PVT_KEY_FAIL, if private key vault update fails.
 *	- XASUFW_KEYMANAGER_UPDATE_PUB_KEY_FAIL, if public key vault update fails.
 *	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_StoreRsaKeyPairInAsuVault(const u8 *KeyData, u32 KeyLen, u8 VaultId)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	u32 PvtKeyId = 0U;
	u32 PubKeyId = 0U;
	XAsu_KeyManagerParams KmParams = {0};

	/** Validate input parameters. */
	if (KeyData == NULL) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	Status = XAsu_RsaValidateKeySize(KeyLen);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	if (VaultId != XKEYMANAGER_ASU_VAULT_ID) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	if (VaultManager.VaultInfo[VaultId].VaultBasePtr == NULL) {
		Status = XASUFW_KEYMANAGER_VAULT_NOT_FOUND;
		goto END;
	}

	/** Set up key manager parameters for vault storage. */
	KmParams.KeyMetadata.Length = (u16)KeyLen;
	KmParams.KeyMetadata.EpochTime = 0U;
	KmParams.KeyMetadata.UsageCount = XASU_KM_USAGE_COUNT_NON_DEPLETING_VALUE;
	KmParams.KeyMetadata.KeyUseCase = XKEYMANAGER_RSA_ALL_KEY_USE_CASES_VALUE;
	KmParams.KeyMetadata.VaultId = VaultId;

	/** Store private key in vault. */
	Status = XKeyManager_UpdateKeyVault(NULL, XASU_RSA_PVT_SUBVAULT_ID, &KmParams,
					    KeyData, &PvtKeyId);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_UPDATE_PVT_KEY_FAIL);
		goto END;
	}

	/** Store public key in vault. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = XKeyManager_UpdateKeyVault(NULL, XASU_RSA_PUB_SUBVAULT_ID, &KmParams,
					    KeyData, &PubKeyId);
	if (Status != XASUFW_SUCCESS) {
		/* Attempt to clean up private key if public key update fails. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_DeleteKey(PvtKeyId, XKEYMANAGER_ASU_SUBSYSTEM_ID);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_KEY_DELETE_ERROR);
			goto END;
		}
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_UPDATE_PUB_KEY_FAIL);
	}

END:
	return Status;
}
/*************************************************************************************************/
/**
 * @brief	Update vault for the provided key identifier.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	KeyType		Sub-vault that contains the key.
 * @param	ParamsPtr	Pointer to the XAsu_KeyManagerParams.
 * @param	KeyData		Pointer to the key data.
 * @param	KeyIdPtr	Pointer to the output Key ID.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault updation is successful.
 * 	- XASUFW_FAILURE, if key vault updation fails.
 * 	- XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED, if the maximum capacity of the sub-vault is reached.
 * 	- XASUFW_MEM_COPY_FAIL, if memory copy operation fails.
 *	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *
 *************************************************************************************************/
static s32 XKeyManager_UpdateKeyVault(XAsufw_Dma *DmaPtr, XAsu_KeyManagerSubVaultType KeyType,
			const XAsu_KeyManagerParams *ParamsPtr, const u8 *KeyData, u32 *KeyIdPtr)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	u32 SubVaultOffset;
	void* SubVaultBasePtr;
	XKeyManager* KeyVaultPtr;
	XAsu_KeyManagerKeyMetadata *KeyMetadataPtr;
	void* KeyContentPtr;
	u16 KeyId = 0U;
	u32 KeyObjectSize = 0U;
	u32 MaxKeySize = 0U;
	u32 KeyCopyLength = 0U;
	u8 VaultId = ParamsPtr->KeyMetadata.VaultId;

	/** Get vault pointer and sub-vault offset. */
	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;
	SubVaultOffset = KeyVaultPtr->SubVaultHeaders[KeyType].Offset;

	/** Calculate sub-vault base address. */
	SubVaultBasePtr = (u8 *)KeyVaultPtr + SubVaultOffset;

	/** Find a free key slot in the sub-vault. */
	Status = XKeyManager_GetFreeKeySlot(KeyVaultPtr, SubVaultBasePtr, &KeyId, KeyType);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	KeyCopyLength = ParamsPtr->KeyMetadata.Length;
	/* Determine maximum key size based on key type. */
	if (KeyType == XASU_AES_SUBVAULT_ID) {
		MaxKeySize = XASU_AES_KEY_SIZE_256BIT_IN_BYTES;
	} else if (KeyType == XASU_IV_SUBVAULT_ID) {
		MaxKeySize = XASU_AES_IV_SIZE_128BIT_IN_BYTES;
	} else if (KeyType == XASU_KDF_HMAC_SUBVAULT_ID) {
		MaxKeySize = XASU_KM_KDF_HMAC_MAX_KEY_LENGTH;
	} else if (KeyType == XASU_RSA_PVT_SUBVAULT_ID) {
		MaxKeySize = XRSA_MAX_KEY_OBJ_SIZE_IN_BYTES;
		KeyCopyLength = XRSA_MAX_KEY_OBJ_SIZE_IN_BYTES;
	} else if (KeyType == XASU_RSA_PUB_SUBVAULT_ID) {
		MaxKeySize = XRSA_4096_KEY_SIZE;
	} else if (KeyType == XASU_ECC_PVT_SUBVAULT_ID) {
		MaxKeySize = XASU_ECC_P521_PVT_KEY_SIZE_IN_BYTES;
	} else if (KeyType == XASU_ECC_PUB_SUBVAULT_ID) {
		MaxKeySize = XASU_ECC_P521_PUB_KEY_SIZE_IN_BYTES;
		KeyCopyLength = XAsu_DoubleCurveLength(ParamsPtr->KeyMetadata.Length);
	} else if (KeyType == XASU_X509_SUBVAULT_ID) {
		MaxKeySize = XASU_X509_MAX_SIZE_IN_BYTES;
	} else if (KeyType == XASU_LMS_SUBVAULT_ID) {
		MaxKeySize = XASU_LMS_MAX_PUB_KEY_SIZE;
	} else {
		/* Do nothing */
	}

	/** Validate key copy length against maximum key size. */
	if (KeyCopyLength > MaxKeySize) {
		Status = XASUFW_KEYMANAGER_INVALID_BUF_SIZE;
		goto END;
	}

	/** Get key object size based on key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];

	/** Calculate pointer to key metadata within sub-vault. */
	KeyMetadataPtr = (XAsu_KeyManagerKeyMetadata *)((u8 *)SubVaultBasePtr + (KeyObjectSize *
						((u32)KeyId - XKEYMANAGER_KEY_INDEX_OFFSET)));

	/** Calculate pointer to key content area. */
	KeyContentPtr = (u8 *)KeyMetadataPtr + sizeof(XAsu_KeyManagerKeyMetadata);

	/** Copy key data to vault using. */
	if (DmaPtr != NULL) {
		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)KeyData, (u64)(UINTPTR)KeyContentPtr,
				       KeyCopyLength, 0U);
	} else {
		Status = Xil_SMemCpy((u8 *)KeyContentPtr, MaxKeySize, KeyData, KeyCopyLength,
				     KeyCopyLength);
	}
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_DMA_COPY_FAIL;
		goto END;
	}

	/** Populate key metadata fields. */
	KeyMetadataPtr->KeyType = (u8)KeyType;
	KeyMetadataPtr->VaultId = VaultId;
	KeyMetadataPtr->KeyId = KeyId;
	KeyMetadataPtr->EpochTime = ParamsPtr->KeyMetadata.EpochTime;
	KeyMetadataPtr->UsageCount = ParamsPtr->KeyMetadata.UsageCount;
	KeyMetadataPtr->Length = ParamsPtr->KeyMetadata.Length;
	KeyMetadataPtr->KeyUseCase = ParamsPtr->KeyMetadata.KeyUseCase;
	KeyMetadataPtr->KeyAttributes = ParamsPtr->KeyMetadata.KeyAttributes;

	/** Increment active keys counter in sub-vault header. */
	KeyVaultPtr->SubVaultHeaders[KeyType].ActiveKeys++;

	/** Generate composite KeyId from components which consists of Key Index, VaultId and Keytype. */
	*KeyIdPtr = (u32)KeyId |
		((u32)KeyType << XKEYMANAGER_KEY_TYPE_SHIFT_VAL) |
		((u32)VaultId << XKEYMANAGER_VAULT_ID_SHIFT_VAL);

	/** Mark key vault as dirty if it is not the ASU vault. */
	if (VaultId != XKEYMANAGER_ASU_VAULT_ID) {
		IsKeyVaultDirty = XKEYMANAGER_VAULT_DIRTY_VALUE;
	}

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Update vault information for a new vault.
 *
 * @param	SubSystemId	Subsystem identifier for the vault.
 * @param	VaultIdPtr	Pointer to the output vault ID.
 *
 * @return
 * 	- XASUFW_SUCCESS, if vault information is updated successfully.
 * 	- XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED, if no space is available for a new vault.
 *
 *************************************************************************************************/
static s32 XKeyManager_UpdateVaultInfo(u32 SubSystemId, u8 *VaultIdPtr)
{
	s32 Status = XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED;
	u8 Index = 0U;

	/** Find a free vault slot. */
	for (Index = 0U; Index < XASU_KM_MAX_VAULTS; Index++) {
		if (VaultManager.VaultInfo[Index].VaultSize == 0U) {
			VaultManager.VaultInfo[Index].SubSystemId = SubSystemId;
			*VaultIdPtr = Index;
			Status = XASUFW_SUCCESS;
			break;
		}
	}

	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Validate key access rights and usage permissions for a given key.
 *
 * @param	SubSystemId	Subsystem identifier requesting key access.
 * @param	SubVaultPtr	Pointer to the sub-vault containing the key.
 * @param	KeyUseCase	Requested use case for the key.
 * @param	VaultId		Identifier of the vault containing the key.
 * @param	IsKeyUsed	TRUE if the key is being used, FALSE if only validated.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key is valid and accessible for the requested use case.
 * 	- XASUFW_KEYMANAGER_ACCESS_DENIED, if subsystem lacks access rights.
 * 	- XASUFW_KEYMANAGER_KEY_EXPIRED, if key usage count is exhausted.
 * 	- XASUFW_KEYMANAGER_KEY_USE_CASE_ERROR, if key use case is not allowed.
 *
 *************************************************************************************************/
static s32 XKeyManager_CheckKeyValidity(u32 SubSystemId, u8 *SubVaultPtr, u8 KeyUseCase, u8 VaultId,
					u8 IsKeyUsed)
{
	s32 Status = XASUFW_FAILURE;
	u8 Index = 0U;
	XAsu_KeyManagerKeyMetadata *MetaDataPtr = (XAsu_KeyManagerKeyMetadata *)SubVaultPtr;
	u16 AllowedAccessRights = 0U;
	XKeyManager *KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;

	AllowedAccessRights = KeyVaultPtr->Header.AccessRights;

	if (SubSystemId == XKEYMANAGER_ASU_SUBSYSTEM_ID) {
		if (AllowedAccessRights != 0U) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END;
		}
	} else {
		if ((!XASU_IS_REDUNDANT_BYTE_VALID(AllowedAccessRights))) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END;
		}
	}

	/** Check for access rights. */
	if (VaultManager.VaultInfo[VaultId].SubSystemId != SubSystemId) {
		for (Index = 0U; Index < XASU_MAX_CHANNELS_SUPPORTED; Index++) {
			if (XKEYMANAGER_IS_BIT_SET(AllowedAccessRights, Index)) {
				if (SubSystemId == XAsu_GetSubsysIdFromIpiMask(XAsu_GetIpiMask(Index))) {
					break;
				}
			}
		}
		if (Index == XASU_MAX_CHANNELS_SUPPORTED) {
			Status = XASUFW_KEYMANAGER_ACCESS_DENIED;
			goto END;
		}
	}

	/** Check for key use case. */
	if ((MetaDataPtr->KeyUseCase & KeyUseCase) == 0U) {
		Status = XASUFW_KEYMANAGER_KEY_USE_CASE_ERROR;
		goto END;
	}

	/*TODO: Update checking with epoch time.*/
	/** Decrement usage count if applicable. */
	if (IsKeyUsed != FALSE) {
		if (MetaDataPtr->UsageCount != XASU_KM_USAGE_COUNT_NON_DEPLETING_VALUE) {
			if (MetaDataPtr->UsageCount == 0U) {
				Status = XASUFW_KEYMANAGER_KEY_EXPIRED;
				goto END;
			}
			MetaDataPtr->UsageCount -= XKEYMANAGER_USAGE_COUNT_DEC_VALUE;

			/** If usage count reaches zero, update counters (key transitions to deactivated). */
			if (MetaDataPtr->UsageCount == 0U) {
				KeyVaultPtr->SubVaultHeaders[MetaDataPtr->KeyType].ActiveKeys--;
				KeyVaultPtr->SubVaultHeaders[MetaDataPtr->KeyType].DeactivatedKeys++;
			}
		}
	}

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Store a raw public key extracted from X.509 certificate to the key vault.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	KeyParams	Pointer to key manager parameters containing vault
 *				information for storing the raw key.
 * @param	CertInfo	Pointer to the parsed X.509 certificate information.
 * @param	RawKeyIdPtr	Pointer to store the generated raw key ID.
 *
 * @return
 *	- XASUFW_SUCCESS, if raw public key is stored successfully.
 *	- XASUFW_FAILURE, in case of failure.
 *	- XASUFW_KEYMANAGER_INVALID_KEY_TYPE, if certificate contains unsupported public key type.
 *
 *************************************************************************************************/
static s32 XKeyManager_StoreX509RawPublicKey(XAsufw_Dma *DmaPtr,
					     const XAsu_KeyManagerParams *KeyParams,
					     const X509_CertInfo *CertInfo, u32 *RawKeyIdPtr)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XAsu_KeyManagerParams RawKeyParams;
	XAsu_KeyManagerSubVaultType RawKeySubVaultType;
	XKeyManager_RsaPubKeyObject *RsaPubKeyObjectPtr;
	u32 KeyLen;
	const u8 *KeyData;
	u32 PubExp = 0U;
	u8 KeyUseCase = 0U;

	/** Determine sub-vault type and key data based on public key type in certificate. */
	if (CertInfo->PublicKeyType == X509_PUB_KEY_ECC) {
		RawKeySubVaultType = XASU_ECC_PUB_SUBVAULT_ID;
		/** Skip one byte indicating compression type. */
		KeyLen = CertInfo->PublicKey.EccPublicKey.PublicKeyLen - XASUFW_VALUE_ONE;

		/*
		 * For ECC public type, key manager is internally doubling the size so divide
		 * the key size by two.
		 */
		KeyLen = KeyLen / XASUFW_VALUE_TWO;

		KeyData = CertInfo->PublicKey.EccPublicKey.PublicKey + XASUFW_BUFFER_INDEX_ONE;

		/**
		 * Map X.509 key usage to ECC public key usage:
		 * - digitalSignature/nonRepudiation -> Signature verification
		 * - keyAgreement -> Key Agreement
		 * - dataEncipherment -> Authentication encryption scheme
		 */
		if (((CertInfo->KeyUsage & XKEYMANAGER_X509_DIGITALSIGNATURE_USE_CASE) != 0U) ||
		    ((CertInfo->KeyUsage & XKEYMANAGER_X509_NONREPUDIATION_USE_CASE) != 0U)) {
			KeyUseCase |= XASU_KEYMANAGER_ECC_PUB_SIGN_VER_USE_CASE;
		}

		if ((CertInfo->KeyUsage & XKEYMANAGER_X509_KEYAGREEMENT_USE_CASE) != 0U) {
			KeyUseCase |= XASU_KEYMANAGER_ECC_PUB_KEY_AGREEMENT_USE_CASE;
		}

		if ((CertInfo->KeyUsage & XKEYMANAGER_X509_DATAENCIPHERMENT_USE_CASE) != 0U) {
			KeyUseCase |= XASU_KEYMANAGER_ECC_PUB_KEY_AUTH_ENC_USE_CASE;
		}
	} else if (CertInfo->PublicKeyType == X509_PUB_KEY_RSA) {
		RawKeySubVaultType = XASU_RSA_PUB_SUBVAULT_ID;
		KeyLen = CertInfo->PublicKey.RsaPublicKey.ModulusLen;
		KeyData = CertInfo->PublicKey.RsaPublicKey.Modulus;

		/**
		 * Map X.509 key usage to RSA public key usage:
		 * - digitalSignature/nonRepudiation -> SIGN_VER
		 * - keyEncipherment/dataEncipherment -> ENCRYPT
		 * - keyAgreement -> KEY_TRANSPORT
		 */
		if (((CertInfo->KeyUsage & XKEYMANAGER_X509_DIGITALSIGNATURE_USE_CASE) != 0U) ||
		    ((CertInfo->KeyUsage & XKEYMANAGER_X509_NONREPUDIATION_USE_CASE) != 0U)) {
			KeyUseCase |= XASU_KEYMANAGER_RSA_PUB_SIGN_VER_USE_CASE;
		}

		if (((CertInfo->KeyUsage & XKEYMANAGER_X509_KEYENCIPHERMENT_USE_CASE) != 0U) ||
		    ((CertInfo->KeyUsage & XKEYMANAGER_X509_DATAENCIPHERMENT_USE_CASE) != 0U)) {
			KeyUseCase |= XASU_KEYMANAGER_RSA_PUB_ENCRYPT_USE_CASE;
		}

		if ((CertInfo->KeyUsage & XKEYMANAGER_X509_KEYAGREEMENT_USE_CASE) != 0U) {
			KeyUseCase |= XASU_KEYMANAGER_RSA_PUB_KEY_TRANSPORT_USE_CASE;
		}

		if (CertInfo->PublicKey.RsaPublicKey.ExponentLen == XASUFW_WORD_LEN_IN_BYTES) {
			/** Extract public exponent from certificate. */
			Status = Xil_SMemCpy(&PubExp, XASUFW_WORD_LEN_IN_BYTES,
					CertInfo->PublicKey.RsaPublicKey.Exponent,
					CertInfo->PublicKey.RsaPublicKey.ExponentLen,
					CertInfo->PublicKey.RsaPublicKey.ExponentLen);
			if (Status != XASUFW_SUCCESS) {
				Status = XASUFW_MEM_COPY_FAIL;
				goto END;
			}
		} else {
			/** Unsupported public exponent size. */
			Status = XASUFW_KEYMANAGER_INVALID_EXPONENT_SIZE;
			goto END;
		}
	} else {
		/** Unsupported public key type. */
		Status = XASUFW_KEYMANAGER_INVALID_KEY_TYPE;
		goto END;
	}

	/** Initialize parameters for raw key storage. */
	RawKeyParams.KeyMetadata.Length = (u16)KeyLen;
	RawKeyParams.KeyMetadata.UsageCount = KeyParams->KeyMetadata.UsageCount;
	RawKeyParams.KeyMetadata.KeyUseCase = KeyUseCase;
	RawKeyParams.KeyMetadata.EpochTime = KeyParams->KeyMetadata.EpochTime;
	RawKeyParams.KeyMetadata.VaultId = KeyParams->KeyMetadata.VaultId;
	RawKeyParams.KeyMetadata.KeyType = (u8)RawKeySubVaultType;
	RawKeyParams.KeyObjectAddr = 0U;
	RawKeyParams.KeyIdAddr = 0U;

	/** Store the raw public key in the appropriate sub-vault. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = XKeyManager_UpdateKeyVault(DmaPtr, RawKeySubVaultType, &RawKeyParams,
					    KeyData, RawKeyIdPtr);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_UPDATE_KEY_VAULT_FAIL);
		goto END;
	}

	/** For RSA keys, additionally store the public exponent (no usage decrement). */
	if (CertInfo->PublicKeyType == X509_PUB_KEY_RSA) {
		RsaPubKeyObjectPtr = (XKeyManager_RsaPubKeyObject *) XKeyManager_GetKeyObjectPtr(*RawKeyIdPtr,
			VaultManager.VaultInfo[KeyParams->KeyMetadata.VaultId].SubSystemId, KeyUseCase, FALSE);
		if (RsaPubKeyObjectPtr == NULL) {
			Status = XASUFW_KEYMANAGER_GET_KEYOBJ_PTR_FAILED;
			goto END;
		}
		RsaPubKeyObjectPtr->PubExp = PubExp;
	}

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Export the exportable key vault to a user-provided address.
 *
 * @param	DmaPtr			Pointer to the XAsufw_Dma instance.
 * @param	AesInstancePtr		Pointer to the XAes instance.
 * @param	ExportAddr		Address where the entire key vault should be exported.
 * @param	ExportBufSize		Size of the buffer at ExportAddr to prevent overflow.
 * @param	ActualVaultSizeAddr	Address where the actual size of exported vault data will
 *					be stored.
 *
 * @return
 *	- XASUFW_SUCCESS, if key vault export is successful.
 *	- XASUFW_CMD_IN_PROGRESS, if DMA transfer is in progress (non-blocking mode).
 *	- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 *	- XASUFW_KEYMANAGER_INVALID_BUF_SIZE, if export buffer is too small.
 *	- XASUFW_KEYMANAGER_AES_KEY_SETUP_FAIL, if AES key setup fails.
 *	- XASUFW_RAND_GEN_ERROR, if TRNG random number generation fails.
 *	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *	- XASUFW_AES_INIT_FAILED, if AES initialization fails.
 *	- XASUFW_AES_UPDATE_FAILED, if AES update fails.
 *	- XASUFW_AES_FINAL_FAILED, if AES finalization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_ExportKeyVault(XAsufw_Dma *DmaPtr, XAes *AesInstancePtr, u64 ExportAddr,
			       u32 ExportBufSize, u32 ActualVaultSizeAddr)
{
	/**
	 * Capture the start time of the key vault export operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	CREATE_VOLATILE(ClearStatus, XASUFW_FAILURE);
	u8 *VaultBaseAddr = XKEYMANAGER_GET_VAULT_BASE_PTR;
	u32 *ActualSizePtr = NULL;
	const XKeyManager *KeyVaultPtr = NULL;
	XAsu_AesParams AesParams;
	XAsu_AesKeyObject KeyObject;
	XKeyManager_VaultMainHeader* VaultHeaderPtr = NULL;
	XFih_Var XFihKeyManager = XFih_VolatileAssignXfihVar(XFIH_FAILURE);
	static u8 VaultIndex = XKEYMANAGER_USER_VAULT_START_INDEX;
	static u32 CurrentOutputOffset = 0U;
	static u8 TagBuf[XKEYMANAGER_AES_TAG_LEN_IN_BYTES];
	static u8 KeyIv[XKEYMANAGER_AES_IV_LEN_IN_BYTES];
	static u32 CmdStage = XKEYMANAGER_AES_CMD_STAGE_INIT;
	static u32 RevokeId = 0U;

	/** Validate input parameters. */
	if ((DmaPtr == NULL) || (AesInstancePtr == NULL) || (ExportAddr == 0U) ||
	    (ExportBufSize < XKEYMANAGER_AES_IV_LEN_IN_BYTES) || (ActualVaultSizeAddr == 0U)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	if (VaultBaseAddr == NULL) {
		Status = XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS;
		goto END;
	}

	/** Get vault header Address. */
	VaultHeaderPtr = (XKeyManager_VaultMainHeader *)VaultBaseAddr;

	switch (CmdStage) {
	case XKEYMANAGER_AES_CMD_STAGE_INIT:
		/** Store and increment the revoke ID for the vault header. */
		if (IsKeyVaultDirty == XKEYMANAGER_VAULT_DIRTY_VALUE) {
			RevokeId = VaultHeaderPtr->RevokeId;
			VaultHeaderPtr->RevokeId++;
		}

		/** Generate KV KEK and write to AES engine for key vault encryption. */
		Status = XKeyManager_GenerateAndWriteAesKey(DmaPtr, AesInstancePtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status,
							  XASUFW_KEYMANAGER_AES_KEY_SETUP_FAIL);
			goto END;
		}

		/** Generate ephemeral Key/IV using TRNG. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XTrng_GetRandomNumbers(KeyIv, XKEYMANAGER_AES_IV_LEN_IN_BYTES);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_RAND_GEN_ERROR;
			goto END_CLR;
		}

		/** Copy IV to export buffer before encryption. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)KeyIv, ExportAddr,
				       XKEYMANAGER_AES_IV_LEN_IN_BYTES, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END_CLR;
		}

		/** Initialize AES parameters. */
		AesParams.EngineMode = (u8)XASU_AES_GCM_MODE;
		AesParams.OperationType = XASU_AES_ENCRYPT_OPERATION;
		AesParams.KeyObjectAddr = (u64)(UINTPTR)&KeyObject;
		AesParams.IvAddr = (u64)(UINTPTR)KeyIv;
		AesParams.IvLen = XKEYMANAGER_AES_IV_LEN_IN_BYTES;
		AesParams.AadAddr = (u64)(UINTPTR)KeyIv;
		AesParams.AadLen = XKEYMANAGER_AES_IV_LEN_IN_BYTES;
		AesParams.TagAddr = (u64)(UINTPTR)TagBuf;
		AesParams.TagLen = XKEYMANAGER_AES_TAG_LEN_IN_BYTES;

		/** Initialize the AES engine for the given AES operation. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Init(AesInstancePtr, DmaPtr, (u64)(UINTPTR)KeyIv,
				   XKEYMANAGER_AES_IV_LEN_IN_BYTES, AesParams.EngineMode,
				   AesParams.OperationType);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_INIT_FAILED);
			goto END_CLR;
		}

		/** Update AAD. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Update(AesInstancePtr, DmaPtr, AesParams.AadAddr, 0U,
				     AesParams.AadLen, XASU_FALSE);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_UPDATE_FAILED);
			goto END_CLR;
		}

		/** Move data offsets to the keyvault data. */
		CurrentOutputOffset = XKEYMANAGER_AES_IV_LEN_IN_BYTES +
					XKEYMANAGER_VAULT_MAIN_HEADER_SIZE;

		/* fall through */
	case XKEYMANAGER_AES_CMD_STAGE_UPDATE_INPROGRESS:
		/** Encrypt each individual vault except ASU vault (index 0). */
		for (; VaultIndex < XASU_KM_MAX_VAULTS; VaultIndex++) {
			KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultIndex].VaultBasePtr;
			if ((VaultManager.VaultInfo[VaultIndex].VaultSize == 0U) ||
			    (!XKEYMANAGER_IS_VAULT_EXPORTABLE(KeyVaultPtr->Header.Restrictions))) {
				/** Skip non-exportable or empty vaults. */
				continue;
			}

			/** Check if the export buffer has enough space for the current vault. */
			if (ExportBufSize < (CurrentOutputOffset + VaultManager.VaultInfo[VaultIndex].VaultSize)) {
				Status = XASUFW_KEYMANAGER_INVALID_BUF_SIZE;
				goto END_CLR;
			}

			/** Encrypt this vault's data. */
			Status = XAes_Update(AesInstancePtr, DmaPtr,
					     (u64)(UINTPTR)KeyVaultPtr,
					     ExportAddr + CurrentOutputOffset,
					     VaultManager.VaultInfo[VaultIndex].VaultSize,
					     XASU_FALSE);

			/** Update offsets after successful synchronous encryption. */
			CurrentOutputOffset += VaultManager.VaultInfo[VaultIndex].VaultSize;

			if (Status == XASUFW_CMD_IN_PROGRESS) {
				/**
				 * DMA transfer is in progress for current vault.
				 * Update offsets and increment VaultIndex for the next iteration.
				 */
				CmdStage = XKEYMANAGER_AES_CMD_STAGE_UPDATE_INPROGRESS;
				VaultIndex++;
				goto END;
			} else if (Status != XASUFW_SUCCESS) {
				Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_UPDATE_FAILED);
				goto END_CLR;
			} else {
				/* Do nothing. */
			}
		}

		/** Encrypt main vault header. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Update(AesInstancePtr, DmaPtr, (u64)(UINTPTR)VaultBaseAddr,
				     ExportAddr + XKEYMANAGER_AES_IV_LEN_IN_BYTES,
				     XKEYMANAGER_VAULT_MAIN_HEADER_SIZE,
				     XASU_TRUE);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_UPDATE_FAILED);
			goto END_CLR;
		}

		/** Finalize AES operation and generate tag. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Final(AesInstancePtr, DmaPtr, (u64)(UINTPTR)TagBuf,
				    XKEYMANAGER_AES_TAG_LEN_IN_BYTES);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_FINAL_FAILED);
			goto END_CLR;
		}

		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		/** Check if the export buffer has enough space for the tag. */
		if (ExportBufSize < (CurrentOutputOffset + XKEYMANAGER_AES_TAG_LEN_IN_BYTES)) {
			Status = XASUFW_KEYMANAGER_INVALID_BUF_SIZE;
			goto END_CLR;
		}

		/** Copy the generated tag to the export buffer after encrypted data. */
		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)TagBuf,
				       ExportAddr + CurrentOutputOffset,
				       XKEYMANAGER_AES_TAG_LEN_IN_BYTES, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END_CLR;
		}

		/** Write actual vault size (IV + encrypted data + GCM tag) to output parameter. */
		ActualSizePtr = (u32 *)(UINTPTR)ActualVaultSizeAddr;
		*ActualSizePtr = CurrentOutputOffset + XKEYMANAGER_AES_TAG_LEN_IN_BYTES;

		/** Mark the key vault as clean after successful export. */
		IsKeyVaultDirty = XKEYMANAGER_VAULT_CLEAN_VALUE;

		break;
	default:
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		break;
	}

	/**
	 * Measure and print the performance time for the key vault export operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END_CLR:
	/** Zeroize the IV buffer. */
	ClearStatus = Xil_SecureZeroize(KeyIv, XKEYMANAGER_AES_IV_LEN_IN_BYTES);
	Status = XAsufw_UpdateErrorStatus(Status, ClearStatus);

	/** Clear the key written to the XASU_AES_USER_KEY_7 key source. */
	XFIH_CALL(XAes_KeyClear, XFihKeyManager, ClearStatus, AesInstancePtr, XASU_AES_USER_KEY_7);
	Status = XAsufw_UpdateErrorStatus(Status, ClearStatus);

	/** Zeroize the key object. */
	ClearStatus = Xil_SecureZeroize((u8 *)(UINTPTR)&KeyObject, sizeof(XAsu_AesKeyObject));
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

	/** Zeroize the local tag buffer. */
	ClearStatus = Xil_SecureZeroize((u8 *)(UINTPTR)TagBuf, XKEYMANAGER_AES_TAG_LEN_IN_BYTES);
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

	/** Reset vault index and offsets after operation. */
	VaultIndex = XKEYMANAGER_USER_VAULT_START_INDEX;
	CurrentOutputOffset = 0U;
	CmdStage = XKEYMANAGER_AES_CMD_STAGE_INIT;

END:
	/** Restore revoke ID in case of failure in key vault export. */
	if ((Status != XASUFW_SUCCESS) && (Status != XASUFW_CMD_IN_PROGRESS) &&
	    (IsKeyVaultDirty == XKEYMANAGER_VAULT_DIRTY_VALUE) && (VaultHeaderPtr != NULL)) {
		VaultHeaderPtr->RevokeId = RevokeId;
	}
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Import the entire key vault from a user-provided address.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	AesInstancePtr	Pointer to the XAes instance.
 * @param	ImportAddr	Address where the encrypted vault data is located.
 * @param	ImportSize	Total size of the import data (including IV and GCM tag).
 *
 * @return
 *	- XASUFW_SUCCESS, if key vault import is successful.
 *	- XASUFW_CMD_IN_PROGRESS, if DMA transfer is in progress (non-blocking mode).
 *	- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 *	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *	- XASUFW_KEYMANAGER_AES_KEY_SETUP_FAIL, if AES key setup fails.
 *	- XASUFW_AES_INIT_FAILED, if AES initialization fails.
 *	- XASUFW_AES_UPDATE_FAILED, if AES update fails.
 *	- XASUFW_AES_FINAL_FAILED, if AES finalization fails.
 *	- XASUFW_KEYMANAGER_INVALID_ID_STRING, if imported vault header contains invalid ID string.
 *	- XASUFW_KEYMANAGER_INVALID_VAULT_VERSION, if imported vault header contains unsupported
 *	version.
 *	- XASUFW_KEYMANAGER_INVALID_VAULT_ID, if imported vault header contains invalid vault ID.
 *
 *************************************************************************************************/
s32 XKeyManager_ImportKeyVault(XAsufw_Dma *DmaPtr, XAes *AesInstancePtr, u64 ImportAddr,
			       u32 ImportSize)
{
	/**
	 * Capture the start time of the key vault import operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	CREATE_VOLATILE(ClearStatus, XASUFW_FAILURE);
	u8 *VaultBaseAddr = XKEYMANAGER_GET_VAULT_BASE_PTR;
	u8 *ActualDataPtr = NULL;
	u8 Index = 0U;
	u8 RevokeId = 0U;
	u8 VaultMainHeader[XKEYMANAGER_VAULT_MAIN_HEADER_SIZE] = {0U};
	u32 VaultId = 0U;
	u32 ImportDataOffset = 0U;
	u32 EncryptedDataSize = 0U;
	XAsu_AesParams AesParams;
	XAsu_AesKeyObject KeyObject;
	XKeyManager *ImportedVaultPtr = NULL;
	XKeyManager_VaultMainHeader *VaultHeaderPtr = NULL;
	const XKeyManager_VaultMainHeader *ImportedVaultHeaderPtr = NULL;
	XFih_Var XFihKeyManager = XFih_VolatileAssignXfihVar(XFIH_FAILURE);
	static u8 KeyIv[XKEYMANAGER_AES_IV_LEN_IN_BYTES];
	static u8 TagBuf[XKEYMANAGER_AES_TAG_LEN_IN_BYTES];
	static u32 CmdStage = XKEYMANAGER_AES_CMD_STAGE_INIT;
	u32 UserVaultLen = 0U;

	/** Validate input parameters. */
	if ((DmaPtr == NULL) || (AesInstancePtr == NULL) || (ImportAddr == 0U) ||
	    (ImportSize < (XKEYMANAGER_AES_IV_LEN_IN_BYTES + XKEYMANAGER_AES_TAG_LEN_IN_BYTES +
			   XKEYMANAGER_VAULT_MAIN_HEADER_SIZE))) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	if (VaultBaseAddr == NULL) {
		Status = XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS;
		goto END;
	}

	/** Get vault header Address. */
	VaultHeaderPtr = (XKeyManager_VaultMainHeader *)VaultBaseAddr;

	/** Calculate encrypted data size (excluding IV and GCM tag). */
	EncryptedDataSize = (ImportSize - XKEYMANAGER_AES_IV_LEN_IN_BYTES -
				XKEYMANAGER_AES_TAG_LEN_IN_BYTES);
	UserVaultLen = VaultManager.TotalVaultSize - XKEYMANAGER_VAULT_MAIN_HEADER_SIZE -
			VaultManager.VaultInfo[XKEYMANAGER_ASU_VAULT_ID].VaultSize ;

	/** Validate sufficient space is available for decrypted data. */
	if (EncryptedDataSize > (UserVaultLen + XKEYMANAGER_VAULT_MAIN_HEADER_SIZE)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Skip ASU key vault to avoid override. */
	ActualDataPtr = VaultBaseAddr + XKEYMANAGER_VAULT_MAIN_HEADER_SIZE +
				VaultManager.VaultInfo[XKEYMANAGER_ASU_VAULT_ID].VaultSize;

	switch (CmdStage) {
	case XKEYMANAGER_AES_CMD_STAGE_INIT:
		/** Clear entire user vault region (excluding ASU vault and main header). */
		Status = Xil_SMemSet(ActualDataPtr, UserVaultLen, 0U, UserVaultLen);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_ZEROIZE_MEMSET_FAIL;
			goto END;
		}

		/** Clear existing vault registry except ASU vault (index 0). */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = Xil_SMemSet(&VaultManager.VaultInfo[XKEYMANAGER_USER_VAULT_START_INDEX],
				     (XASU_KM_MAX_VAULTS - XKEYMANAGER_USER_VAULT_START_INDEX) *
				     sizeof(XKeyManager_VaultInfo), 0U,
				     (XASU_KM_MAX_VAULTS - XKEYMANAGER_USER_VAULT_START_INDEX) *
				     sizeof(XKeyManager_VaultInfo));
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_ZEROIZE_MEMSET_FAIL;
			goto END;
		}
		VaultManager.RemainingVaultSize = VaultManager.TotalVaultSize -
					XKEYMANAGER_VAULT_MAIN_HEADER_SIZE -
					VaultManager.VaultInfo[XKEYMANAGER_ASU_VAULT_ID].VaultSize;

		/** Generate KV KEK and write to AES engine for key vault decryption. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_GenerateAndWriteAesKey(DmaPtr, AesInstancePtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status,
							  XASUFW_KEYMANAGER_AES_KEY_SETUP_FAIL);
			goto END;
		}

		/** Read IV from the beginning of import buffer. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr, ImportAddr, (u64)(UINTPTR)KeyIv,
				       XKEYMANAGER_AES_IV_LEN_IN_BYTES, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END_CLR;
		}

		/** Read GCM tag from the end of import buffer. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr,
				       ImportAddr + ImportSize - XKEYMANAGER_AES_TAG_LEN_IN_BYTES,
				       (u64)(UINTPTR)TagBuf, XKEYMANAGER_AES_TAG_LEN_IN_BYTES, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END_CLR;
		}

		/** Initialize AES parameters for decryption. */
		AesParams.EngineMode = (u8)XASU_AES_GCM_MODE;
		AesParams.OperationType = XASU_AES_DECRYPT_OPERATION;
		AesParams.KeyObjectAddr = (u64)(UINTPTR)&KeyObject;
		AesParams.IvAddr = (u64)(UINTPTR)KeyIv;
		AesParams.IvLen = XKEYMANAGER_AES_IV_LEN_IN_BYTES;
		AesParams.AadAddr = (u64)(UINTPTR)KeyIv;
		AesParams.AadLen = XKEYMANAGER_AES_IV_LEN_IN_BYTES;
		AesParams.TagAddr = (u64)(UINTPTR)TagBuf;
		AesParams.TagLen = XKEYMANAGER_AES_TAG_LEN_IN_BYTES;

		/** Initialize the AES engine for decryption. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Init(AesInstancePtr, DmaPtr, (u64)(UINTPTR)KeyIv,
				   XKEYMANAGER_AES_IV_LEN_IN_BYTES, AesParams.EngineMode,
				   AesParams.OperationType);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_INIT_FAILED);
			goto END_CLR;
		}

		/** Update AAD with IV for ensuring authenticity and integrity. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Update(AesInstancePtr, DmaPtr, AesParams.AadAddr, 0U,
				     AesParams.AadLen, XASU_FALSE);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_UPDATE_FAILED);
			goto END_CLR;
		}

		if (EncryptedDataSize > XKEYMANAGER_VAULT_MAIN_HEADER_SIZE) {
			ImportDataOffset = XKEYMANAGER_AES_IV_LEN_IN_BYTES +
						XKEYMANAGER_VAULT_MAIN_HEADER_SIZE;
			/** Decrypt the vault data (excluding IV at start and GCM tag at end). */
			ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
			Status = XAes_Update(AesInstancePtr, DmaPtr, ImportAddr + ImportDataOffset,
					     (u64)(UINTPTR)ActualDataPtr,
					     EncryptedDataSize - XKEYMANAGER_VAULT_MAIN_HEADER_SIZE,
					     XASU_FALSE);
			if (Status == XASUFW_CMD_IN_PROGRESS) {
				CmdStage = XKEYMANAGER_AES_CMD_STAGE_UPDATE_INPROGRESS;
				goto END;
			} else if (Status != XASUFW_SUCCESS) {
				Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_UPDATE_FAILED);
				goto END_CLR;
			} else {
				/* Do nothing. */
			}
		}

		/* fall through */
	case XKEYMANAGER_AES_CMD_STAGE_UPDATE_INPROGRESS:
		/** Decrypt main vault header. */
		Status = XAes_Update(AesInstancePtr, DmaPtr,
				     ImportAddr + XKEYMANAGER_AES_IV_LEN_IN_BYTES,
				     (u64)(UINTPTR)VaultMainHeader,
				     XKEYMANAGER_VAULT_MAIN_HEADER_SIZE, XASU_TRUE);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_UPDATE_FAILED);
			goto END_CLR;
		}

		/** Finalize AES operation and validate GCM tag. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAes_Final(AesInstancePtr, DmaPtr, (u64)(UINTPTR)TagBuf,
				    XKEYMANAGER_AES_TAG_LEN_IN_BYTES);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status, XASUFW_AES_FINAL_FAILED);
			goto END_CLR;
		}

		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		/** Map decrypted main header to the vault header structure. */
		ImportedVaultHeaderPtr = (XKeyManager_VaultMainHeader *)VaultMainHeader;

		/** Validate key vault identification string. */
		if (ImportedVaultHeaderPtr->KeyVaultHeader != XKEYMANAGER_IDENTIFICATION_STRING) {
			Status = XASUFW_KEYMANAGER_INVALID_ID_STRING;
			goto END_CLR;
		}

		/** Validate major and minor version. */
		if ((ImportedVaultHeaderPtr->MajorVersion != XKEYMANAGER_MAJOR_VERSION) ||
		    (ImportedVaultHeaderPtr->MinorVersion != XKEYMANAGER_MINOR_VERSION)) {
			Status = XASUFW_KEYMANAGER_INVALID_VAULT_VERSION;
			goto END_CLR;
		}

		/** Reconstruct vault main header. */
		VaultHeaderPtr->KeyVaultHeader = ImportedVaultHeaderPtr->KeyVaultHeader;
		VaultHeaderPtr->MajorVersion = ImportedVaultHeaderPtr->MajorVersion;
		VaultHeaderPtr->MinorVersion = ImportedVaultHeaderPtr->MinorVersion;
		VaultHeaderPtr->RevokeId= ImportedVaultHeaderPtr->RevokeId;

		/**
		 * Validate revoke ID to detect importing older vault versions that should have been
		 * revoked. The revoke ID in the vault header is incremented whenever key vault is
		 * exported after updation. User is expected to revoke earlier version of key vault by
		 * programmatically burning efuse revoke bits before importing newer version of
		 * key vault. If the revoke ID in the imported vault is less than the current
		 * revoke ID, it indicates that this vault version has been revoked and should not
		 * be imported.
		 */
		RevokeId = XKeyManager_GetRevokeId();
		if (ImportedVaultHeaderPtr->RevokeId <= RevokeId) {
			Status = XASUFW_KEYMANAGER_VAULT_REVOKE_ID_MISMATCH;
			goto END_CLR;
		}

		/** Update remaining vault size after clearing existing vaults. */
		VaultManager.RemainingVaultSize = VaultManager.TotalVaultSize -
					VaultManager.VaultInfo[XKEYMANAGER_ASU_VAULT_ID].VaultSize -
					EncryptedDataSize;

		/**
		 * Scan through decrypted vault to rebuild vault info.
		 * First vault starts after the main header, subsequent vaults are contiguous.
		 */
		ImportedVaultPtr = (XKeyManager *)ActualDataPtr;

		for (Index = XKEYMANAGER_USER_VAULT_START_INDEX; (Index < XASU_KM_MAX_VAULTS);
		     Index++) {
			/** Check if the current vault is empty. */
			if (ImportedVaultPtr->Header.VaultSize == 0U) {
				break;
			}

			/** Get the vault ID from the imported vault header. */
			VaultId = ImportedVaultPtr->Header.KeyVaultId;
			if (VaultId >= XASU_KM_MAX_VAULTS) {
				Status = XASUFW_KEYMANAGER_INVALID_VAULT_ID;
				goto END_CLR;
			}

			/** Reconstruct vault info from imported vault header. */
			VaultManager.VaultInfo[VaultId].VaultBasePtr = (u32 *)ImportedVaultPtr;
			VaultManager.VaultInfo[VaultId].VaultSize = ImportedVaultPtr->Header.VaultSize;
			VaultManager.VaultInfo[VaultId].SubSystemId = ImportedVaultPtr->Header.SubSystemId;

			/** Move to next vault. */
			ImportedVaultPtr = (XKeyManager *)((u8 *)ImportedVaultPtr +
					   ImportedVaultPtr->Header.VaultSize);
		}

		Status = XASUFW_SUCCESS;
		break;
	default:
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		break;
	}

	/**
	 * Measure and print the performance time for the key vault import operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END_CLR:
	/** Zeroize the IV buffer. */
	ClearStatus = Xil_SecureZeroize(KeyIv, XKEYMANAGER_AES_IV_LEN_IN_BYTES);
	Status = XAsufw_UpdateErrorStatus(Status, ClearStatus);

	/** Clear the key written to the XASU_AES_USER_KEY_7 key source. */
	XFIH_CALL(XAes_KeyClear, XFihKeyManager, ClearStatus, AesInstancePtr, XASU_AES_USER_KEY_7);
	Status = XAsufw_UpdateErrorStatus(Status, ClearStatus);

	/** Zeroize the key object. */
	ClearStatus = Xil_SecureZeroize((u8 *)(UINTPTR)&KeyObject, sizeof(XAsu_AesKeyObject));
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

	/** Zeroize the local tag buffer. */
	ClearStatus = Xil_SecureZeroize((u8 *)(UINTPTR)TagBuf, XKEYMANAGER_AES_TAG_LEN_IN_BYTES);
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

	/** Reset command stage after decryption is complete. */
	CmdStage = XKEYMANAGER_AES_CMD_STAGE_INIT;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Read the offchip revoke efuse registers to determine the current revoke ID,
 *		which is represented by the index of the last bit set across the 8 revoke registers.
 *
 * @return
 *	- Return revoke ID as the index of the last efuse bit set.
 *	- If no bits are set, returns 0.
 *
 *************************************************************************************************/
static inline u8 XKeyManager_GetRevokeId(void)
{
	u32 RegValue;
	u8 LastBitIndex = 0U;
	s8 RegIdx;
	s8 BitIdx;

	/**
	 * Scan through all 8 registers (32 bytes = 256 bits total) starting from the
	 * last register and MSB to find the index of the last (highest) bit that is set.
	 */
	for (RegIdx = XKEYMANAGER_MAX_REVOKE_REG_INDEX; RegIdx >= 0; RegIdx--) {
		RegValue = XAsufw_ReadReg(EFUSE_CACHE_OFFCHIP_REVOKE_0_ADDR + ((u32)RegIdx *
					  XASUFW_WORD_LEN_IN_BYTES));

		/** Check each bit in the current register from MSB to LSB. */
		if (RegValue != 0U) {
			for (BitIdx = XASUFW_MAX_BIT_INDEX_IN_WORD; BitIdx >= 0; BitIdx--) {
				if ((RegValue & ((u32)1U << (u8)BitIdx)) != 0U) {
					LastBitIndex = ((u8)RegIdx * XASUFW_BITS_IN_WORD) + (u8)BitIdx;
					goto END;
				}
			}
		}
	}

END:
	return LastBitIndex;
}

/*************************************************************************************************/
/**
 * @brief	This function generates a KEK (Key Encryption Key) from efuse black key 0 and
 *		writes it to the AES engine for key vault encryption/decryption.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	AesInstancePtr	Pointer to the XAes instance.
 *
 * @return
 *	- XASUFW_SUCCESS, if KEK generation and write is successful.
 *	- XASUFW_KEYMANAGER_KV_KEK_GEN_FAIL, if KEK generation fails.
 *
 *************************************************************************************************/
static s32 XKeyManager_GenerateAndWriteAesKey(XAsufw_Dma *DmaPtr, XAes *AesInstancePtr)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	CREATE_VOLATILE(ClearStatus, XASUFW_FAILURE);
	XFih_Var XFihKeyManager = XFih_VolatileAssignXfihVar(XFIH_FAILURE);
	u8 KeyVaultKek[XKEYMANAGER_AES_KEK_LEN_IN_BYTES] = {0U};
	XAsu_AesKeyObject KeyObject;
	const char *CtxStr = XKEYMANAGER_KV_KEK_CONTEXT;
	const u8 *Context = (const u8 *)CtxStr;

	/** Generate KEK from efuse black key 0 using predefined constants. */
	Status = XKdf_GenerateKekFromEfuse0(DmaPtr, AesInstancePtr, Context,
					    XKEYMANAGER_KV_KEK_CONTEXT_LEN,
					    XKEYMANAGER_KV_KEK_IV_INC_VAL,
					    XKEYMANAGER_AES_KEK_LEN_IN_BYTES, KeyVaultKek);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_KV_KEK_GEN_FAIL);
		goto END;
	}

	/** Setup AES key object for writing the generated KEK. */
	KeyObject.KeySize = (u32)XASU_AES_KEY_SIZE_256_BITS;
	KeyObject.KeySrc = XASU_AES_USER_KEY_7;
	KeyObject.KeyAddress = (u64)(UINTPTR)KeyVaultKek;
	KeyObject.KeyId = 0U;

	/** Write the generated KEK to the AES USER_KEY_7 register. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = XAes_WriteKey(AesInstancePtr, DmaPtr, &KeyObject);

	/** Securely zeroize the KEK buffer. */
	XFIH_CALL(Xil_SecureZeroize, XFihKeyManager, ClearStatus, KeyVaultKek, XKEYMANAGER_AES_KEK_LEN_IN_BYTES);
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Retrieve LMS public key object from key vault if KeyId is provided.
 *
 * @param	KeyObjectAddr	Address where LMS public key object should be copied.
 * @param	SubSystemId	Subsystem ID of the requested subsystem.
 * @param	KeyUsecase	Requested use case for the key.
 * @param	KeyId		Composite key identifier.
 *
 * @return
 *		- XASUFW_SUCCESS, if key resolution is successful.
 *		- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 *		- XASUFW_KEYMANAGER_GET_KEYOBJ_PTR_FAILED, if get key object pointer failed.
 *
 *************************************************************************************************/
static s32 XKeyManager_UpdateLmsPubKeyObjectFromVault(u64 KeyObjectAddr, u32 SubSystemId,
	u8 KeyUsecase, u32 KeyId)
{
	s32 Status = XASUFW_FAILURE;
	XKeyManager_LmsPubKeyObject *KeyVaultKeyObjectPtr;
	XAsu_LmsHssKeyObject *KeyObject;

	/** Validate input parameters. */
	if (KeyObjectAddr == 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	KeyObject = (XAsu_LmsHssKeyObject *)(UINTPTR)KeyObjectAddr;

	/** Get key vault key object pointer based on the KeyId, SubSystemId, and KeyUsecase. */
	KeyVaultKeyObjectPtr = (XKeyManager_LmsPubKeyObject *)XKeyManager_GetKeyObjectPtr(KeyId,
									SubSystemId, KeyUsecase, TRUE);
	if (KeyVaultKeyObjectPtr == NULL) {
		Status = XASUFW_KEYMANAGER_GET_KEYOBJ_PTR_FAILED;
		goto END;
	}

	KeyObject->PubKeyAddr = (u64)(UINTPTR)KeyVaultKeyObjectPtr->PublicKey;
	KeyObject->PubKeyLen = KeyVaultKeyObjectPtr->Metadata.Length;

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

