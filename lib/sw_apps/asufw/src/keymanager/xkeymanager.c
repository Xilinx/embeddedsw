/**
 * @brief	Initialize the key manager configuration by reading vault parameters from RTCA.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault initialization is successful.
 * 	- XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS, if configured address is invalid.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization of vault registry fails.
 * 	- XASUFW_KEYMANAGER_NO_SPACE, if total vault size is insufficient to accommodate main header
 * 					and sub-vaults.
 *
 *************************************************************************************************/
s32 XKeyManager_CfgInitialize(void)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XKeyManager_VaultMainHeader* VaultHeaderPtr;
	u32 TotalSizeAllocated = 0U;
	u32 Index = 0U;
	u32 AllocatedDdrSize = 0U;

	/** Validate that vault base address is within lower DDR memory. */
	if (XAsufw_ReadReg(XASU_RTCA_DDR_BASEADDR_HIGH) != 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS;
		goto END;
	}

	Status = Xil_SMemSet(&VaultManager, sizeof(XKeyManager_VaultRegistry), 0U,
			sizeof(XKeyManager_VaultRegistry));
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	/** Initialize vault size from RTCA registers. */
	AllocatedDdrSize = XAsufw_ReadReg(XASU_RTCA_DDR_SIZE_ADDR);
	if (AllocatedDdrSize == 0U) {
		VaultManager.TotalVaultSize = 0U;
	} else if (AllocatedDdrSize < XASUFW_DDR_RSVD_SIZE) {
		Status = XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS;
		goto END;
	} else {
		VaultManager.TotalVaultSize = AllocatedDdrSize - XASUFW_DDR_RSVD_SIZE;

		/** Initialize the reserved DDR area to zero. */
		Status = Xil_SMemSet((u8 *)(XAsufw_ReadReg(XASU_RTCA_DDR_BASEADDR_LOW)),
				XASUFW_DDR_RSVD_SIZE, 0U, XASUFW_DDR_RSVD_SIZE);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_ZEROIZE_MEMSET_FAIL;
			goto END;
		}
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	if (VaultManager.TotalVaultSize != 0U) {

		VaultHeaderPtr = (XKeyManager_VaultMainHeader *)XKEYMANAGER_GET_VAULT_BASE_PTR;

		/** Calculate total size allocated for vaults including ASU vault based on RTCA register values. */
		for (Index = 0U; Index <= XASU_MAX_CHANNELS_SUPPORTED; Index++) {
			TotalSizeAllocated += XAsufw_ReadReg(XASU_RTCA_KEYVAULT_CHNL_MAX_SIZE_BASE_ADDR + (Index * XASUFW_WORD_LEN_IN_BYTES));
		}

		if (VaultManager.TotalVaultSize < (TotalSizeAllocated + XKEYMANAGER_VAULT_MAIN_HEADER_SIZE)) {
			Status = XASUFW_KEYMANAGER_NO_SPACE;
			goto END;
		}

		VaultManager.RemainingVaultSize = VaultManager.TotalVaultSize -
							XKEYMANAGER_VAULT_MAIN_HEADER_SIZE;

		/** Initialize vault header with identification and version information. */
		VaultHeaderPtr->KeyVaultHeader = XKEYMANAGER_IDENTIFICATION_STRING;
		VaultHeaderPtr->MajorVersion = XKEYMANAGER_MAJOR_VERSION;
		VaultHeaderPtr->MinorVersion = XKEYMANAGER_MINOR_VERSION;
		VaultHeaderPtr->RevokeId = 0x00U;
	}

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Retrieve a pointer to a key object within the vault using its composite key
 * 		identifier. This function extracts the vault ID, key type, and key index from the
 * 		composite key identifier, validates the parameters, and calculates the memory
 * 		address of the corresponding key object within the appropriate sub-vault.
 *
 * 		The composite KeyId is generated by combining three components:
 * 		- Bits [15:0]: Key index within the sub-vault (1-based)
 * 		- Bits [23:16]: Key type identifier (AES, IV, RSA, ECC, etc.)
 * 		- Bits [31:24]: Vault ID identifying which vault contains the key
 *
 * @param	KeyId		Composite key identifier containing VaultId (bits [31:24]),
 * 						KeyType (bits [23:16]), and KeyId (bits [15:0]).
 * @param	SubSystemId	Subsystem identifier for which the key is requested.
 * @param	KeyUsecase	Use case identifier for the key.
 * @param	IsKeyUsed	TRUE if the key is being used, FALSE if only validated.
 *
 * @return
 * 	- Pointer to the key object on success
 * 	- NULL if the key ID is invalid or not found.
 *
 *************************************************************************************************/
u8* XKeyManager_GetKeyObjectPtr(u32 KeyId, u32 SubSystemId, u8 KeyUsecase, u8 IsKeyUsed)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XKeyManager* KeyVaultPtr;
	u32 VaultId = XKEYMANAGER_EXTRACT_VAULTID(KeyId);
	u32 KeyType = XKEYMANAGER_EXTRACT_KEYTYPE(KeyId);
	u32 KeyIndex = XKEYMANAGER_EXTRACT_KEYINDEX(KeyId);
	u32 KeyObjectSize = 0U;
	u8* SubVaultPtr = NULL;
	u8* TempPtr = NULL;

	/** Validate composite KeyId and extracted components. */
	if ((KeyId == 0U) || (VaultId >= XASU_KM_MAX_VAULTS) ||
		(KeyType >= XKEYMANAGER_MAX_SUB_VAULTS) || (KeyIndex == 0U)) {
		goto END;
	}

	if (VaultManager.VaultInfo[VaultId].VaultSize == 0U) {
		goto END;
	}

	/** Get vault base address from vault info. */
	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;

	if (KeyIndex > KeyVaultPtr->SubVaultHeaders[KeyType].Capacity) {
		goto END;
	}

	/** Get size of key object for this key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];

	/** Calculate pointer to key object within sub-vault. */
	TempPtr = (u8 *)KeyVaultPtr + (KeyVaultPtr->SubVaultHeaders[KeyType].Offset) +
			(KeyObjectSize * (KeyIndex - XKEYMANAGER_KEY_INDEX_OFFSET));

	Status = XKeyManager_CheckKeyValidity(SubSystemId, TempPtr, KeyUsecase, (u8)VaultId,
					      IsKeyUsed);
	if ((Status == XASUFW_SUCCESS) && (Status == XASUFW_SUCCESS)) {
		SubVaultPtr = TempPtr;
	}

END:
	return SubVaultPtr;
}

/*************************************************************************************************/
/**
 * @brief	This function creates and initializes a key vault and its sub-vault headers.
 *
 * @param	ParamsPtr	Pointer to the XAsu_KeyManagerSubVaultParams.
 * @param	SubsystemId	Subsystem ID for which the vault is being created.
 * @param	IpiMask		IPI mask to identify the channel for this vault.
 * @param	VaultIdPtr	Pointer to store the created vault ID.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault creation is successful.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameters are invalid.
 * 	- XASUFW_KEYMANAGER_NO_SPACE, if no vault space is available.
 * 	- XASUFW_KEYMANAGER_UPDATE_INFO_FAIL, if vault information update fails.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_CreateKeyVault(const XAsu_KeyManagerSubVaultParams *ParamsPtr, u32 SubsystemId,
				u32 IpiMask, u32 *VaultIdPtr)
{
	/**
	 * Capture the start time of the key vault creation operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	s32 ClearStatus = XASUFW_FAILURE;
	XKeyManager* KeyVaultPtr;
	const u16 *SubVaultCapacityPtr = NULL;
	u32 Index = 0U;
	u32 TotalSubVaultSize = 0U;
	u32 ReqVaultSize = 0U;
	u32 KeyObjectSize = 0U;
	u8 VaultId = 0U;
	u32 CurrentOffset;
	u32 MaxSizeAllocated = 0U;
	u32 CumulativeUsedSize = 0U;
	u8 VaultIdx = 0U;

	/** Validate the input parameters. */
	if ((ParamsPtr == NULL) || (VaultIdPtr == NULL)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	Status = XAsu_KmValidateVaultCreateParams(ParamsPtr);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	if (SubsystemId == XKEYMANAGER_ASU_SUBSYSTEM_ID) {
		if (ParamsPtr->AccessRights != 0U) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END;
		}
	} else {
		if ((!XASU_IS_REDUNDANT_BYTE_VALID(ParamsPtr->AccessRights))) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END;
		}
	}
	SubVaultCapacityPtr = &ParamsPtr->AESKeyVaultCapacity;

	/*TODO: Update sizes of other key objects.*/

	/** Calculate total size needed for all sub-vaults based on their capacities. */
	TotalSubVaultSize = (ParamsPtr->AESKeyVaultCapacity * sizeof(XKeyManager_AesKeyObject)) +
			    (ParamsPtr->IVVaultCapacity * sizeof(XKeyManager_IvObject)) +
			    (ParamsPtr->KDFHmacKeyVaultCapacity * sizeof(XKeyManager_KdfHmacKeyObject)) +
			    (ParamsPtr->RSAPvtKeyVaultCapacity * sizeof(XKeyManager_RsaPvtKeyObject)) +
			    (ParamsPtr->RSAPubKeyVaultCapacity * sizeof(XKeyManager_RsaPubKeyObject)) +
			    (ParamsPtr->ECCPvtKeyVaultCapacity * sizeof(XKeyManager_EccPvtKeyObject)) +
			    (ParamsPtr->ECCPubKeyVaultCapacity * sizeof(XKeyManager_EccPubKeyObject)) +
			    (ParamsPtr->LMSKeyVaultCapacity * sizeof(XKeyManager_LmsPubKeyObject)) +
			    (ParamsPtr->X509KeyVaultCapacity * sizeof(XKeyManager_X509KeyObject));

	/* Ensure at least one sub-vault has non-zero capacity. */
	if (TotalSubVaultSize == 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Calculate total vault size including headers. */
	ReqVaultSize = TotalSubVaultSize + XKEYMANAGER_HEADER_SIZE +
		(XKEYMANAGER_MAX_SUB_VAULTS * XKEYMANAGER_SUB_VAULT_HEADER_SIZE);

	/**
	 * During the Keyvault export, keyvault is encrypted using AES. In certain cases, AES need
	 * data block in multiple of 16 bytes. Add required padding to ensure that, vault size is
	 * in multiple of 16 bytes if it is not already a multiple of 16 bytes.
	 */
	if ((ReqVaultSize % XKEYMANAGER_AES_BLOCK_SIZE) != 0U) {
		ReqVaultSize += (XKEYMANAGER_AES_BLOCK_SIZE -
					(ReqVaultSize % XKEYMANAGER_AES_BLOCK_SIZE));
	}

	if (IpiMask != 0U) {
		for (Index = 0U; Index < XASU_MAX_CHANNELS_SUPPORTED; Index++) {
			if (IpiMask == XAsu_GetIpiMask(Index)) {
				break;
			}
		}
		if (Index == XASU_MAX_CHANNELS_SUPPORTED) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END;
		}
	} else {
		Index = XKEYMANAGER_ASU_DDR_SIZE_INDEX;
	}

	MaxSizeAllocated = XAsufw_ReadReg(XASU_RTCA_KEYVAULT_CHNL_MAX_SIZE_BASE_ADDR + (Index * XASUFW_WORD_LEN_IN_BYTES));

	/** Calculate cumulative vault size already used by this channel/subsystem. */
	for (VaultIdx = 0U; VaultIdx < XASU_KM_MAX_VAULTS; VaultIdx++) {
		if ((VaultManager.VaultInfo[VaultIdx].VaultSize != 0U) &&
		    (VaultManager.VaultInfo[VaultIdx].SubSystemId == SubsystemId)) {
			CumulativeUsedSize += VaultManager.VaultInfo[VaultIdx].VaultSize;
		}
	}

	/** Check if sufficient space is available per-channel . */
	if ((ReqVaultSize > VaultManager.RemainingVaultSize) ||
	    ((CumulativeUsedSize + ReqVaultSize) > MaxSizeAllocated)) {
		Status = XASUFW_KEYMANAGER_NO_SPACE;
		goto END;
	}

	/** Update sub-system ID and vault ID to vault info for a subsystem. */
	Status = XKeyManager_UpdateVaultInfo(SubsystemId, &VaultId);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_UPDATE_INFO_FAIL);
		goto END;
	}

	*VaultIdPtr = VaultId;

	/** Calculate vault base address in DDR memory, past the internal reserved DDR memory. */
	KeyVaultPtr = (XKeyManager *)(XKEYMANAGER_GET_VAULT_BASE_PTR +
					VaultManager.TotalVaultSize - VaultManager.RemainingVaultSize);

	/** Register vault base address and size in vault info. */
	VaultManager.VaultInfo[VaultId].VaultBasePtr = (u32 *)KeyVaultPtr;
	VaultManager.VaultInfo[VaultId].VaultSize = ReqVaultSize;

	/* Initialize entire vault memory to zero for security. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = Xil_SMemSet(KeyVaultPtr, ReqVaultSize, 0U, ReqVaultSize);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
		goto END_CLR;
	}

	/* Update remaining vault space. */
	VaultManager.RemainingVaultSize -= ReqVaultSize;

	/** Initialize vault header. */
	KeyVaultPtr->Header.KeyVaultId = VaultId;
	KeyVaultPtr->Header.Restrictions = ParamsPtr->Restrictions;
	KeyVaultPtr->Header.AccessRights = ParamsPtr->AccessRights;
	KeyVaultPtr->Header.VaultSize = ReqVaultSize;
	KeyVaultPtr->Header.SubSystemId = SubsystemId;

	/* Calculate initial offset past vault header and all sub-vault headers. */
	CurrentOffset = XKEYMANAGER_HEADER_SIZE +
			(XKEYMANAGER_MAX_SUB_VAULTS * XKEYMANAGER_SUB_VAULT_HEADER_SIZE);

	/** Initialize sub-vault offsets and capacity. */
	for (Index = 0U; Index < XKEYMANAGER_MAX_SUB_VAULTS; Index++) {
		if (SubVaultCapacityPtr[Index] != 0U) {
			KeyVaultPtr->SubVaultHeaders[Index].Offset = CurrentOffset;
			KeyVaultPtr->SubVaultHeaders[Index].Capacity = SubVaultCapacityPtr[Index];

			/* Advance offset by the size of all objects in this sub-vault. */
			KeyObjectSize = XKeyManager_KeyObjectSizeLookup[Index];
			CurrentOffset += (KeyObjectSize * SubVaultCapacityPtr[Index]);
		}
	}

	/** Mark key vault as dirty if it is not the ASU vault. */
	if (VaultId != XKEYMANAGER_ASU_VAULT_ID) {
		IsKeyVaultDirty = XKEYMANAGER_VAULT_DIRTY_VALUE;
	}

	/**
	 * Measure and print the performance time for the key vault creation operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END_CLR:
	/* Clear vault info on failure. */
	if (Status != XASUFW_SUCCESS) {
		ClearStatus = Xil_SMemSet(&VaultManager.VaultInfo[VaultId], sizeof(XKeyManager_VaultInfo),
					0U, sizeof(XKeyManager_VaultInfo));
		if (ClearStatus != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(ClearStatus, XASUFW_ZEROIZE_MEMSET_FAIL);
		}
	}
END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	This function deletes the key vault and compacts memory by shifting subsequent
 *		vaults to fill the gap.
 *
 * @param	SubsystemId	Subsystem ID for which the vault is being deleted.
 * @param	VaultId		Vault ID of the subsystem to be deleted.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault deletion is successful.
 * 	- XASUFW_MEM_COPY_FAIL, if memory copy operation fails during compaction.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_DeleteKeyVault(u32 SubsystemId, u32 VaultId)
{
	/**
	 * Capture the start time of the key vault deletion operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	u8 Index = 0U;
	u32 DelVaultSize = 0U;
	u32 *DelVaultBasePtr = NULL;
	u8 *ResidualDataPtr = NULL;
	const u8 *ShiftSrcPtr = NULL;
	u32 ShiftSize = 0U;
	u8 *VaultBaseAddr = NULL;

	/** Validate the vault ID for the given subsystem. */
	if (VaultId >= XASU_KM_MAX_VAULTS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Validate vault access for the subsystem. */
	if (VaultManager.VaultInfo[VaultId].SubSystemId != SubsystemId) {
		Status = XASUFW_KEYMANAGER_INVALID_VAULT_ACCESS;
		goto END;
	}

	/** Store vault information before deletion. */
	DelVaultSize = VaultManager.VaultInfo[VaultId].VaultSize;
	DelVaultBasePtr = VaultManager.VaultInfo[VaultId].VaultBasePtr;

	/** Calculate vault memory region boundaries, past the internal reserved DDR memory. */
	VaultBaseAddr = XKEYMANAGER_GET_VAULT_BASE_PTR;

	/**
	 * Compact memory with a single contiguous copy.
	 * Shift all data after the target vault down to fill the gap.
	 */
	ShiftSrcPtr = (u8 *)DelVaultBasePtr + DelVaultSize;
	ShiftSize = (u32)((VaultBaseAddr + VaultManager.TotalVaultSize - VaultManager.RemainingVaultSize) -
			ShiftSrcPtr);

	if (ShiftSize > 0U) {
		/** Move all subsequent vault data in one operation (regions may overlap). */
		Status = Xil_SMemMove(DelVaultBasePtr, ShiftSize, ShiftSrcPtr, ShiftSize, ShiftSize);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_MEM_COPY_FAIL;
			goto END;
		}

		/** Update base pointers for all vaults that were shifted. */
		for (Index = 0U; Index < XASU_KM_MAX_VAULTS; Index++) {
			if ((Index != VaultId) && (VaultManager.VaultInfo[Index].VaultSize != 0U)) {
				if (VaultManager.VaultInfo[Index].VaultBasePtr > DelVaultBasePtr) {
					VaultManager.VaultInfo[Index].VaultBasePtr =
						(u32 *)((u8 *)VaultManager.VaultInfo[Index].VaultBasePtr -
							DelVaultSize);
				}
			}
		}
	}

	/**
	 * Securely zeroize the freed region at the end to clear residual data.
	 * After compaction, freed space is at: (used area end) for DelVaultSize bytes.
	 */
	ResidualDataPtr = VaultBaseAddr + VaultManager.TotalVaultSize - VaultManager.RemainingVaultSize -
			DelVaultSize;

	/** Return the vault size back to available space. */
	VaultManager.RemainingVaultSize += DelVaultSize;
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = Xil_SecureZeroize(ResidualDataPtr, DelVaultSize);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
	}

	/** Clear the vault's info entry. */
	if (Xil_SMemSet(&VaultManager.VaultInfo[VaultId], sizeof(XKeyManager_VaultInfo),
				0U, sizeof(XKeyManager_VaultInfo)) != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
	}

	if (Status != XASUFW_SUCCESS) {
		XFIH_GOTO(END);
	}

	/** Mark key vault as dirty if it is not the ASU vault. */
	if (VaultId != XKEYMANAGER_ASU_VAULT_ID) {
		IsKeyVaultDirty = XKEYMANAGER_VAULT_DIRTY_VALUE;
	}

	/**
	 * Measure and print the performance time for the key vault deletion operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	This function deletes a single key from the vault. Only the vault owner can
 *		delete keys from the vault.
 *
 * @param	KeyId		Composite key identifier containing VaultId (bits [31:24]),
 *				KeyType (bits [23:16]), and KeyIndex (bits [15:0]).
 * @param	SubSystemId	Subsystem ID requesting the deletion (must be vault owner).
 *
 * @return
 * 	- XASUFW_SUCCESS, if key deletion is successful.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameters are invalid.
 * 	- XASUFW_KEYMANAGER_VAULT_NOT_FOUND, if vault is not found.
 * 	- XASUFW_KEYMANAGER_ACCESS_DENIED, if subsystem is not the vault owner.
 * 	- XASUFW_KEYMANAGER_KEY_NOT_FOUND, if key is not found or already deleted.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_DeleteKey(u32 KeyId, u32 SubSystemId)
{
	/**
	 * Capture the start time of the key deletion operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XKeyManager *KeyVaultPtr = NULL;
	u32 VaultId = XKEYMANAGER_EXTRACT_VAULTID(KeyId);
	u32 KeyType = XKEYMANAGER_EXTRACT_KEYTYPE(KeyId);
	u32 KeyIndex = XKEYMANAGER_EXTRACT_KEYINDEX(KeyId);
	u32 KeyObjectSize = 0U;
	u8 *KeyObjectPtr = NULL;
	const XAsu_KeyManagerKeyMetadata *MetadataPtr = NULL;

	/** Validate composite KeyId and extracted components. */
	if ((KeyId == 0U) || (VaultId >= XASU_KM_MAX_VAULTS) ||
	    (KeyType >= XKEYMANAGER_MAX_SUB_VAULTS) || (KeyIndex == 0U)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Validate vault exists. */
	if (VaultManager.VaultInfo[VaultId].VaultSize == 0U) {
		Status = XASUFW_KEYMANAGER_VAULT_NOT_FOUND;
		goto END;
	}

	/** Check ownership - only vault owner can delete keys. */
	if (VaultManager.VaultInfo[VaultId].SubSystemId != SubSystemId) {
		Status = XASUFW_KEYMANAGER_ACCESS_DENIED;
		goto END;
	}

	/** Get vault base address. */
	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;

	/** Validate key index is within sub-vault capacity. */
	if (KeyIndex > KeyVaultPtr->SubVaultHeaders[KeyType].Capacity) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Get size of key object for this key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];
	if (KeyObjectSize == 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_KEY_TYPE;
		goto END;
	}

	/** Calculate pointer to key object within sub-vault. */
	KeyObjectPtr = (u8 *)KeyVaultPtr + KeyVaultPtr->SubVaultHeaders[KeyType].Offset +
			(KeyObjectSize * (KeyIndex - XKEYMANAGER_KEY_INDEX_OFFSET));

	/** Check if key exists (KeyId in metadata should match the key index). */
	MetadataPtr = (const XAsu_KeyManagerKeyMetadata *)KeyObjectPtr;
	if (MetadataPtr->KeyId != KeyIndex) {
		Status = XASUFW_KEYMANAGER_KEY_NOT_FOUND;
		goto END;
	}

	/** Update sub-vault header counters based on key state. */
	if (MetadataPtr->UsageCount > 0U) {
		/** Key is active (has remaining uses or is non-depleting). */
		KeyVaultPtr->SubVaultHeaders[KeyType].ActiveKeys--;
	} else {
		/** Key was deactivated (expired). */
		KeyVaultPtr->SubVaultHeaders[KeyType].DeactivatedKeys--;
	}

	/** Securely zeroize the entire key object (metadata + content). */
	Status = Xil_SecureZeroize(KeyObjectPtr, KeyObjectSize);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
		XFIH_GOTO(END);
	}

	/** Mark key vault as dirty if it is not the ASU vault. */
	if (VaultId != XKEYMANAGER_ASU_VAULT_ID) {
		IsKeyVaultDirty = XKEYMANAGER_VAULT_DIRTY_VALUE;
	}

	/**
	 * Measure and print the performance time for the key deletion operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Generate an ephemeral Key/IV and optionally export it via DMA.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	ParamsPtr	Pointer to XAsu_KeyManagerParams.
 * @param	KeyIdPtr	Pointer to the output Key ID.
 * @param	SubSystemId	Subsystem ID for which the key is being generated.
 * @param	KeyType		Type of key to be generated.
 *
 * @return
 * 	- XASUFW_SUCCESS, when the key is generated and transferred or updated in the vault.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 * 	- XASUFW_KEYMANAGER_INVALID_KEY_TYPE, if the command ID does not correspond to a valid key type.
 * 	- XASUFW_RAND_GEN_ERROR, if TRNG generation fails.
 * 	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *
 *************************************************************************************************/
s32 XKeyManager_GenerateKeyIv(XAsufw_Dma *DmaPtr,
			const XAsu_KeyManagerParams *ParamsPtr, u32 *KeyIdPtr, u32 SubSystemId,
			XAsu_KeyManagerSubVaultType KeyType)
{
	/**
	 * Capture the start time of the key/IV generation operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	s32 ClearStatus = XASUFW_FAILURE;
	u8 EphemeralData[XASU_KM_KDF_HMAC_MAX_KEY_LENGTH];

	/** Validate the input parameters. */
	if ((ParamsPtr == NULL) || (DmaPtr == NULL) || (KeyIdPtr == NULL) ||
	    (KeyType >= XKEYMANAGER_MAX_SUB_VAULTS)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Validate key length based on key type. */
	Status = XAsu_KmValidateKeyLength(ParamsPtr->KeyMetadata.Length, (u8)KeyType);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Generate ephemeral Key/IV using TRNG. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = XTrng_GetRandomNumbers(EphemeralData, ParamsPtr->KeyMetadata.Length);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_RAND_GEN_ERROR;
		goto END_CLR;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	if (ParamsPtr->KeyObjectAddr != 0U) {
		/** Copy key to user address via DMA if address is provided. */
		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)EphemeralData,
				       ParamsPtr->KeyObjectAddr, ParamsPtr->KeyMetadata.Length, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
		}
	} else {
		Status = XAsu_KmValidateVaultParams(ParamsPtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END_CLR;
		}

		/** Validate vault access for the subsystem. */
		if (VaultManager.VaultInfo[ParamsPtr->KeyMetadata.VaultId].SubSystemId != SubSystemId) {
			Status = XASUFW_KEYMANAGER_INVALID_VAULT_ACCESS;
			goto END_CLR;
		}

		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		/** Store key in vault if no address is provided. */
		Status = XKeyManager_UpdateKeyVault(DmaPtr, KeyType, ParamsPtr, EphemeralData,
						    KeyIdPtr);
	}

	/**
	 * Measure and print the performance time for the key/IV generation operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END_CLR:
	/* Securely zeroize ephemeral key data. */
	ClearStatus = Xil_SecureZeroize(EphemeralData, XASU_KM_KDF_HMAC_MAX_KEY_LENGTH);
	if (ClearStatus != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(ClearStatus, XASUFW_ZEROIZE_MEMSET_FAIL);
	}
END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Generate an RSA key pair and optionally export it via DMA.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	ParamsPtr	Pointer to XAsu_KeyManagerParams.
 * @param	KeyIdPtr	Pointer to the output Key IDs.
 * @param	SubSystemId	Subsystem ID for which the key is being generated.
 *
 * @return
 * 	- XASUFW_SUCCESS, when the key is generated and transferred or updated in the vault.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 * 	- XASUFW_RSA_KEY_PAIR_GENERATION_FAIL, if RSA key pair generation fails.
 * 	- XASUFW_KEYMANAGER_UPDATE_PVT_KEY_FAIL, if updating private key in vault fails.
 * 	- XASUFW_KEYMANAGER_UPDATE_PUB_KEY_FAIL, if updating public key in vault fails.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 * 	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *
 *************************************************************************************************/
s32 XKeyManager_GenerateRsaKeyPair(XAsufw_Dma *DmaPtr, const XAsu_KeyManagerParams *ParamsPtr,
			u32 *KeyIdPtr, u32 SubSystemId)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XFih_Var XFihRsaKey = XFih_VolatileAssignXfihVar(XFIH_FAILURE);
	s32 ClearStatus = XASUFW_FAILURE;
	u8 *ModulusArr = XRsa_GetDataBlockAddr();

	/** Validate the input parameters. */
	if ((ParamsPtr == NULL) || (DmaPtr == NULL)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	if ((KeyIdPtr == NULL) && (ParamsPtr->KeyObjectAddr == 0U)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Validate key length parameters. */
	Status = XAsu_KmValidateKeyLength(ParamsPtr->KeyMetadata.Length, XASU_KM_RSA_KEYTYPE);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	/** Key attribute should be set to indicate presence of prime components for RSA private key. */
	if (ParamsPtr->KeyMetadata.KeyAttributes != XRSA_PRIME_NUM_IS_PRSNT) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Fetch a pre-generated RSA key pair from the ASU vault. */
	Status = XKeyManager_FetchRsaKeyPairFromAsuVault(DmaPtr, ModulusArr, XKEYMANAGER_ASU_VAULT_ID,
				ParamsPtr->KeyObjectAddr);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_RSA_KEY_PAIR_GENERATION_FAIL);
		goto END;
	}

	if (ParamsPtr->KeyObjectAddr == 0U) {
		/** Store the key in the vault. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsu_KmValidateVaultParams(ParamsPtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END_CLR;
		}
		/** Validate vault access for the subsystem. */
		if (VaultManager.VaultInfo[ParamsPtr->KeyMetadata.VaultId].SubSystemId != SubSystemId) {
			Status = XASUFW_KEYMANAGER_INVALID_VAULT_ACCESS;
			goto END_CLR;
		}

		/** - Update the private key to key vault and store the KeyId to (KeyIdPtr + 1U). */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_UpdateKeyVault(DmaPtr, XASU_RSA_PVT_SUBVAULT_ID, ParamsPtr,
					ModulusArr, (KeyIdPtr + XKEYMANAGER_PVT_KEY_ID_OFFSET));
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status,
					XASUFW_KEYMANAGER_UPDATE_PVT_KEY_FAIL);
			goto END_CLR;
		}

		/** - Update the public key to key vault and store the KeyId to KeyIdPtr. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_UpdateKeyVault(DmaPtr, XASU_RSA_PUB_SUBVAULT_ID, ParamsPtr,
					ModulusArr, KeyIdPtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status,
					XASUFW_KEYMANAGER_UPDATE_PUB_KEY_FAIL);
		}
	}

END_CLR:
	/* Securely zeroize key data. */
	XFIH_CALL(Xil_SecureZeroize, XFihRsaKey, ClearStatus, (u8 *)(UINTPTR)ModulusArr,
			XRSA_MAX_KEY_OBJ_SIZE_IN_BYTES);
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Generate ECC key pair and optionally export it via DMA.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	ParamsPtr	Pointer to XAsu_KeyManagerParams.
 * @param	KeyIdPtr	Pointer to the output Key IDs.
 * @param	SubSystemId	Subsystem ID for which the key is being generated.
 *
 * @return
 * 	- XASUFW_SUCCESS, when the key is generated and transferred or updated in the vault.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 * 	- XASUFW_KEYMANAGER_INVALID_VAULT_ACCESS, if the subsystem does not have access to the vault.
 * 	- XASUFW_ECC_KEY_PAIR_GENERATION_FAIL, if ECC key pair generation fails.
 * 	- XASUFW_KEYMANAGER_UPDATE_PVT_KEY_FAIL, if updating private key in vault fails.
 * 	- XASUFW_KEYMANAGER_UPDATE_PUB_KEY_FAIL, if updating public key in vault fails.
 * 	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *
 *************************************************************************************************/
s32 XKeyManager_GenerateEccKeyPair(XAsufw_Dma *DmaPtr, const XAsu_KeyManagerParams *ParamsPtr,
			u32 *KeyIdPtr, u32 SubSystemId)
{
	/**
	 * Capture the start time of the ECC key pair generation operation, if performance
	 * measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_START(XASU_MODULE_KEYMANAGER_ID);

	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XFih_Var XFihEccKey = XFih_VolatileAssignXfihVar(XFIH_FAILURE);
	s32 ClearStatus = XASUFW_FAILURE;
	XAsu_EccKeyPairObject *EccKeyPairObjectPtr = NULL;
	u8 PvtKey[XASU_ECC_P521_PVT_KEY_SIZE_IN_BYTES];
	u8 PubKey[XASU_ECC_P521_PUB_KEY_SIZE_IN_BYTES];

	/** Validate the input parameters. */
	if ((ParamsPtr == NULL) || (DmaPtr == NULL)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	if ((KeyIdPtr == NULL) && (ParamsPtr->KeyObjectAddr == 0U)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	Status = XAsu_EccValidateCurveInfo(ParamsPtr->KeyMetadata.KeyAttributes,
				ParamsPtr->KeyMetadata.Length);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	/** Generate ECC key pair. */
	Status = XRsa_EccGenKeyPair(DmaPtr, ParamsPtr->KeyMetadata.KeyAttributes,
				ParamsPtr->KeyMetadata.Length, (u64)(UINTPTR)PvtKey,
				(u64)(UINTPTR)PubKey);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_ECC_KEY_PAIR_GENERATION_FAIL);
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	if (ParamsPtr->KeyObjectAddr != 0U) {
		/**
		 * If KeyObjectAddr is provided, copy the key to the provided address using DMA.
		 */
		EccKeyPairObjectPtr = (XAsu_EccKeyPairObject *)(UINTPTR)ParamsPtr->KeyObjectAddr;

		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)PubKey,
				       (u64)(UINTPTR)EccKeyPairObjectPtr->PublicKey,
				       XAsu_DoubleCurveLength(ParamsPtr->KeyMetadata.Length), 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
			goto END_CLR;
		}

		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)PvtKey,
				       (u64)(UINTPTR)EccKeyPairObjectPtr->PrivateKey,
				       ParamsPtr->KeyMetadata.Length, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
		}
	} else {
		/** Else, store the key in the vault. */
		Status = XAsu_KmValidateVaultParams(ParamsPtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END_CLR;
		}

		/** Validate vault access for the subsystem. */
		if (VaultManager.VaultInfo[ParamsPtr->KeyMetadata.VaultId].SubSystemId != SubSystemId) {
			Status = XASUFW_KEYMANAGER_INVALID_VAULT_ACCESS;
			goto END_CLR;
		}

		/** - Update the private key to key vault and store the KeyId to (KeyIdPtr + 1U). */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_UpdateKeyVault(DmaPtr, XASU_ECC_PVT_SUBVAULT_ID, ParamsPtr,
					PvtKey, (KeyIdPtr + XKEYMANAGER_PVT_KEY_ID_OFFSET));
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status,
					XASUFW_KEYMANAGER_UPDATE_PVT_KEY_FAIL);
			goto END_CLR;
		}

		/** - Update the public key to key vault and store the KeyId to KeyIdPtr. */
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_UpdateKeyVault(DmaPtr, XASU_ECC_PUB_SUBVAULT_ID, ParamsPtr,
					PubKey, KeyIdPtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(Status,
					XASUFW_KEYMANAGER_UPDATE_PUB_KEY_FAIL);
		}
	}

	/**
	 * Measure and print the performance time for the ECC key pair generation operation, if
	 * performance measurement is enabled.
	 */
	XASUFW_MEASURE_PERF_STOP(XASU_MODULE_KEYMANAGER_ID);

END_CLR:
	/* Securely zeroize key data. */
	XFIH_CALL(Xil_SecureZeroize, XFihEccKey, ClearStatus, (u8 *)(UINTPTR)PubKey,
			XASU_ECC_P521_PUB_KEY_SIZE_IN_BYTES);
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

	XFIH_CALL(Xil_SecureZeroize, XFihEccKey, ClearStatus, (u8 *)(UINTPTR)PvtKey,
			XASU_ECC_P521_PVT_KEY_SIZE_IN_BYTES);
	Status = XAsufw_UpdateBufStatus(Status, ClearStatus);

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	This function update key object from vault based on key type.
 *
 * @param	DmaPtr			Pointer to the XAsufw_Dma instance.
 * @param	KeyId			Composite key identifier.
 * @param	KeyObjectAddr		Address of key object structure.
 * @param	SubSystemId		Subsystem ID of the requested subsystem.
 * @param	KeyUsecase		Requested use case for the key.
 * @param	RsaOpType		RSA operation type (NONE, RSA_NONCRT, RSA_CRT).
 *
 * @return
 *	- XASUFW_SUCCESS, if key object update is successful.
 *	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameters are invalid.
 *	- XASUFW_KEYMANAGER_INVALID_KEY_TYPE, if key type is not supported.
 *	- XASUFW_KEYMANAGER_GET_KEYOBJ_PTR_FAILED, if get key object pointer failed.
 *
 *************************************************************************************************/
s32 XKeyManager_UpdateKeyObjFromVault(XAsufw_Dma *DmaPtr, u32 KeyId, u64 KeyObjectAddr,
				      u32 SubSystemId, u8 KeyUsecase,
				      XKeyManager_RsaOpType RsaOpType)
{
	s32 Status = XASUFW_FAILURE;
	u8 KeyType;
	u32 ActualKeyId = KeyId;
	const u8 *X509KeyObjectPtr = NULL;
	const XKeyManager_X509KeyObject *X509KeyObj = NULL;

	/** Validate input parameters. */
	if ((DmaPtr == NULL) || (KeyObjectAddr == 0U) || (KeyId == 0U) ||
	    (RsaOpType > XKEYMANAGER_RSA_OP_CRT)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Extract key type from composite KeyId. */
	KeyType = XKEYMANAGER_EXTRACT_KEYTYPE(KeyId);

	/** Handle X.509 certificate keys by extracting raw key ID and type. */
	if (KeyType == (u8)XASU_X509_SUBVAULT_ID) {
		/** Get X.509 certificate object pointer from vault. */
		X509KeyObjectPtr = XKeyManager_GetKeyObjectPtr(KeyId, SubSystemId, KeyUsecase, TRUE);
		if (X509KeyObjectPtr == NULL) {
			Status = XASUFW_KEYMANAGER_GET_KEYOBJ_PTR_FAILED;
			goto END;
		}

		/** Extract raw key ID from X.509 certificate object. */
		X509KeyObj = (XKeyManager_X509KeyObject *)X509KeyObjectPtr;
		ActualKeyId = X509KeyObj->RawKeyId;

		/** Override KeyType with the raw key type. */
		KeyType = XKEYMANAGER_EXTRACT_KEYTYPE(ActualKeyId);
	}

	/** Call appropriate update function based on key type. */
	switch (KeyType) {
	case XASU_AES_SUBVAULT_ID:
		/** Update AES key object from vault. */
		Status = XKeyManager_UpdateAesKeyObjectFromVault(
							(XAsu_AesKeyObject *)(UINTPTR)KeyObjectAddr,
							SubSystemId,
							KeyUsecase);
		break;
	case XASU_IV_SUBVAULT_ID:
		/** Update IV object from vault. */
		Status = XKeyManager_UpdateIvObjectFromVault(KeyObjectAddr, SubSystemId,
							     KeyUsecase, ActualKeyId);
		break;
	case XASU_KDF_HMAC_SUBVAULT_ID:
		/** Update KDF/HMAC key object from vault. */
		Status = XKeyManager_UpdateKdfHmacKeyFromVault(KeyObjectAddr, SubSystemId,
							       KeyUsecase, ActualKeyId);
		break;
	case XASU_RSA_PVT_SUBVAULT_ID:
		if (RsaOpType == XKEYMANAGER_RSA_OP_CRT) {
			/** Update RSA CRT private key object from vault. */
			Status = XKeyManager_UpdateRsaCrtPvtKeyObjectFromVault(DmaPtr,
									       KeyObjectAddr,
									       SubSystemId,
									       KeyUsecase,
									       ActualKeyId);
		} else if (RsaOpType == XKEYMANAGER_RSA_OP_NONCRT) {
			/** Update RSA Non-CRT private key object from vault. */
			Status = XKeyManager_UpdateRsaPvtKeyObjectFromVault(DmaPtr, KeyObjectAddr,
									    SubSystemId,
									    KeyUsecase,
									    ActualKeyId);
		} else {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		}
		break;
	case XASU_RSA_PUB_SUBVAULT_ID:
		/** Update RSA public key object from vault. */
		Status = XKeyManager_UpdateRsaPubKeyObjectFromVault(DmaPtr, KeyObjectAddr,
								    SubSystemId,
								    KeyUsecase,
								    ActualKeyId);
		break;
	case XASU_ECC_PUB_SUBVAULT_ID:
		/** Update ECC public key object from vault. */
		Status = XKeyManager_UpdateEccPubKeyObjectFromVault((XAsu_EccKeyObject *)(UINTPTR)KeyObjectAddr,
								    SubSystemId,
								    KeyUsecase,
								    ActualKeyId);
		break;
	case XASU_ECC_PVT_SUBVAULT_ID:
		/** Update ECC private key object from vault. */
		Status = XKeyManager_UpdateEccPvtKeyObjectFromVault((XAsu_EccKeyObject *)(UINTPTR)KeyObjectAddr,
								    SubSystemId,
								    KeyUsecase,
								    ActualKeyId);
		break;
	case XASU_LMS_SUBVAULT_ID:
		/** Update LMS public key object from vault. */
		Status = XKeyManager_UpdateLmsPubKeyObjectFromVault(KeyObjectAddr, SubSystemId,
								    KeyUsecase,
								    ActualKeyId);
		break;
	default:
		/** Invalid key type. */
		Status = XASUFW_KEYMANAGER_INVALID_KEY_TYPE;
		break;
	}

END:
	return Status;
}

