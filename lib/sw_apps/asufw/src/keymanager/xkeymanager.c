XKeyManager_VaultRegistry VaultManager; /**< Vault info which contains info for each vault of a
					subsystem and also total and remaining vault size */

/**< Lookup table for key object sizes based on key type */
static const u32 XKeyManager_KeyObjectSizeLookup[XKEYMANAGER_MAX_SUB_VAULTS] = {
	[XKEYMANAGER_AES_SUBVAULT_ID] = sizeof(XKeyManager_AesKeyObject),
	[XKEYMANAGER_IV_SUBVAULT_ID] = sizeof(XKeyManager_IvObject),
	[XKEYMANAGER_RSA_PVT_SUBVAULT_ID] = 0U,
	[XKEYMANAGER_RSA_PUB_SUBVAULT_ID] = 0U,
	[XKEYMANAGER_ECC_PVT_SUBVAULT_ID] = 0U,
	[XKEYMANAGER_ECC_PUB_SUBVAULT_ID] = 0U,
	[XKEYMANAGER_KDF_SUBVAULT_ID] = 0U,
	[XKEYMANAGER_LMS_SUBVAULT_ID] = 0U,
	[XKEYMANAGER_X509_SUBVAULT_ID] = 0U
};

/*************************************************************************************************/
/**
 * @brief	Initialize the key manager configuration by reading vault parameters from RTCA.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault initialization is successful.
 * 	- XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS, if configured address is invalid.
 *
 *************************************************************************************************/
s32 XKeyManager_CfgInitialize(void)
{
	s32 Status = XASUFW_FAILURE;

	/** Validate that vault base address is within lower DDR memory. */
	if (XAsufw_ReadReg(XASU_RTCA_KEYVAULT_BASEADDR_HIGH) != 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_DDR_ADDRESS;
		goto END;
	}

	/*TODO: Compare DDR capacity.*/

	Status = Xil_SMemSet(&VaultManager, sizeof(XKeyManager_VaultRegistry), 0U,
			sizeof(XKeyManager_VaultRegistry));
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
		goto END;
	}

	/** Initialize vault size from RTCA registers. */
	VaultManager.TotalVaultSize = XAsufw_ReadReg(XASU_RTCA_KEYVAULT_SIZE_ADDR);
	VaultManager.RemainingVaultSize = XAsufw_ReadReg(XASU_RTCA_KEYVAULT_SIZE_ADDR);
	Status = XASUFW_SUCCESS;
END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Retrieve a pointer to a key object within the vault using its composite key
 * 		identifier. This function extracts the vault ID, key type, and key index from the
 * 		composite key identifier, validates the parameters, and calculates the memory
 * 		address of the corresponding key object within the appropriate sub-vault.
 *
 * 		The composite KeyId is generated by combining three components:
 * 		- Bits [15:0]: Key index within the sub-vault (1-based)
 * 		- Bits [23:16]: Vault ID identifying which vault contains the key
 * 		- Bits [31:24]: Key type identifier (AES, IV, RSA, ECC, etc.)
 *
 * @param	KeyId		Composite key identifier containing VaultId (bits [23:16]),
 * 						KeyType (bits [31:24]), and KeyId (bits [15:0]).
 * @param	SubSystemId	Subsystem identifier for which the key is requested.
 * @param	KeyUsecase	Use case identifier for the key.
 *
 * @return
 * 	- Pointer to the key object on success
 * 	- NULL if the key ID is invalid or not found.
 *
 *************************************************************************************************/
u8* XKeyManager_GetKeyObjectPtr(u32 KeyId, u32 SubSystemId, u8 KeyUsecase)
{
	s32 Status = XASUFW_FAILURE;
	XKeyManager* KeyVaultPtr;
	u32 VaultId = XKEYMANAGER_EXTRACT_VAULTID(KeyId);
	u32 KeyType = XKEYMANAGER_EXTRACT_KEYTYPE(KeyId);
	u32 KeyIndex = XKEYMANAGER_EXTRACT_KEYINDEX(KeyId);
	u32 KeyObjectSize = 0U;
	u8* SubVaultPtr = NULL;

	/** Validate composite KeyId and extracted components. */
	if ((KeyId == 0U) || (VaultId >= XKEYMANAGER_MAX_VAULTS) ||
		(KeyType >= XKEYMANAGER_MAX_SUB_VAULTS) || (KeyIndex == 0U)) {
		goto END;
	}

	if (VaultManager.VaultInfo[VaultId].VaultSize == 0U) {
		goto END;
	}

	/** Get vault base address from vault info. */
	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;

	if (KeyIndex > KeyVaultPtr->SubVaultHeaders[KeyType].Capacity) {
		goto END;
	}

	/** Get size of key object for this key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];

	/** Calculate pointer to key object within sub-vault. */
	SubVaultPtr = (u8 *)KeyVaultPtr + (KeyVaultPtr->SubVaultHeaders[KeyType].Offset) +
			(KeyObjectSize * (KeyIndex - XKEYMANAGER_KEY_INDEX_OFFSET));

	Status = XKeyManager_CheckKeyValidity(SubSystemId, SubVaultPtr, KeyUsecase, (u8)VaultId);
	if (Status != XASUFW_SUCCESS) {
		SubVaultPtr = NULL;
	}
END:
	return SubVaultPtr;
}

/*************************************************************************************************/
/**
 * @brief	This function creates and initializes a key vault and its sub-vault headers.
 *
 * @param	ParamsPtr	Pointer to the XAsu_KeyManagerSubVaultParams.
 * @param	SubsystemId	Subsystem ID for which the vault is being created.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault creation is successful.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameters are invalid.
 * 	- XASUFW_KEYMANAGER_NO_SPACE, if no vault space is available.
 * 	- XASUFW_KEYMANAGER_UPDATE_INFO_FAIL, if vault information update fails.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_CreateKeyVault(const XAsu_KeyManagerSubVaultParams *ParamsPtr, u32 SubsystemId)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	s32 ClearStatus = XASUFW_FAILURE;
	XKeyManager* KeyVaultPtr;
	const u16 *SubVaultCapacityPtr = &ParamsPtr->AESKeyVaultCapacity;
	u32 Index = 0U;
	u32 TotalSubVaultSize = 0U;
	u32 ReqVaultSize = 0U;
	u32 KeyObjectSize = 0U;
	u8 VaultId = 0U;
	u32 CurrentOffset;

	/** Validate the input parameters. */
	if (ParamsPtr == NULL) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/*TODO: Update sizes of other key objects.*/

	/** Calculate total size needed for all sub-vaults based on their capacities. */
	TotalSubVaultSize = (ParamsPtr->AESKeyVaultCapacity * sizeof(XKeyManager_AesKeyObject)) +
			    (ParamsPtr->IVVaultCapacity * sizeof(XKeyManager_IvObject)) +
			    ParamsPtr->RSAPvtKeyVaultCapacity +
			    ParamsPtr->RSAPubKeyVaultCapacity +
			    ParamsPtr->ECCPvtKeyVaultCapacity +
			    ParamsPtr->ECCPubKeyVaultCapacity +
			    ParamsPtr->KDFKeyVaultCapacity +
			    ParamsPtr->LMSKeyVaultCapacity + ParamsPtr->X509KeyVaultCapacity;

	/* Ensure at least one sub-vault has non-zero capacity. */
	if (TotalSubVaultSize == 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Calculate total vault size including headers. */
	ReqVaultSize = TotalSubVaultSize + XKEYMANAGER_HEADER_SIZE +
		(XKEYMANAGER_MAX_SUB_VAULTS * XKEYMANAGER_SUB_VAULT_HEADER_SIZE);

	/** Check if sufficient space is available. */
	if (ReqVaultSize > VaultManager.RemainingVaultSize) {
		Status = XASUFW_KEYMANAGER_NO_SPACE;
		goto END;
	}

	/** Update sub-system ID and vault ID to vault info for a subsystem. */
	Status = XKeyManager_UpdateVaultInfo(SubsystemId, &VaultId);
	if (Status != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(Status, XASUFW_KEYMANAGER_UPDATE_INFO_FAIL);
		goto END;
	}

	/** Calculate vault base address in DDR memory. */
	KeyVaultPtr = (XKeyManager *)((u8 *)(XAsufw_ReadReg(XASU_RTCA_KEYVAULT_BASEADDR_LOW)) +
				VaultManager.TotalVaultSize - VaultManager.RemainingVaultSize);

	/** Register vault base address and size in vault info. */
	VaultManager.VaultInfo[VaultId].VaultBasePtr = (u32 *)KeyVaultPtr;
	VaultManager.VaultInfo[VaultId].VaultSize = ReqVaultSize;

	/* Initialize entire vault memory to zero for security. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = Xil_SMemSet(KeyVaultPtr, ReqVaultSize, 0U, ReqVaultSize);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
		goto END_CLR;
	}

	/* Update remaining vault space. */
	VaultManager.RemainingVaultSize -= ReqVaultSize;

	/** Initialize vault header with identification and version information. */
	KeyVaultPtr->Header.KeyVaultHeader = XKEYMANAGER_IDENTIFICATION_STRING;
	KeyVaultPtr->Header.MajorVersion = XKEYMANAGER_MAJOR_VERSION;
	KeyVaultPtr->Header.MinorVersion = XKEYMANAGER_MINOR_VERSION;

	/* Calculate initial offset past vault header and all sub-vault headers. */
	CurrentOffset = XKEYMANAGER_HEADER_SIZE +
			(XKEYMANAGER_MAX_SUB_VAULTS * XKEYMANAGER_SUB_VAULT_HEADER_SIZE);

	/** Initialize sub-vault offsets and capacity. */
	for (Index = 0U; Index < XKEYMANAGER_MAX_SUB_VAULTS; Index++) {
		if (SubVaultCapacityPtr[Index] != 0U) {
			KeyVaultPtr->SubVaultHeaders[Index].Offset = CurrentOffset;
			KeyVaultPtr->SubVaultHeaders[Index].Capacity = SubVaultCapacityPtr[Index];

			/* Advance offset by the size of all objects in this sub-vault. */
			KeyObjectSize = XKeyManager_KeyObjectSizeLookup[Index];
			CurrentOffset += (KeyObjectSize * SubVaultCapacityPtr[Index]);
		}
	}

END_CLR:
	/* Clear vault info on failure. */
	if (Status != XASUFW_SUCCESS) {
		ClearStatus = Xil_SMemSet(&VaultManager.VaultInfo[VaultId], sizeof(XKeyManager_VaultInfo),
					0U, sizeof(XKeyManager_VaultInfo));
		if (ClearStatus != XASUFW_SUCCESS) {
			Status = XAsufw_UpdateErrorStatus(ClearStatus, XASUFW_ZEROIZE_MEMSET_FAIL);
		}
	}
END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	This function deletes the key vault and compacts memory by shifting subsequent
 *		vaults to fill the gap.
 *
 * @param	SubsystemId	Subsystem ID for which the vault is being deleted.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault deletion is successful.
 * 	- XASUFW_KEYMANAGER_VAULT_NOT_FOUND, if vault for subsystem is not found.
 * 	- XASUFW_MEM_COPY_FAIL, if memory copy operation fails during compaction.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_DeleteKeyVault(u32 SubsystemId)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	u8 VaultId = 0U;
	u8 Index = 0U;
	u32 DelVaultSize = 0U;
	u32 *DelVaultBasePtr = NULL;
	u8 *ResidualDataPtr = NULL;
	u8 *ShiftSrcPtr = NULL;
	u32 ShiftSize = 0U;
	u8 *VaultBaseAddr = NULL;

	/** Find the vault ID for the given subsystem. */
	Status = XKeyManager_GetVaultId(SubsystemId, &VaultId);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_VAULT_NOT_FOUND;
		goto END;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	/** Store vault information before deletion. */
	DelVaultSize = VaultManager.VaultInfo[VaultId].VaultSize;
	DelVaultBasePtr = VaultManager.VaultInfo[VaultId].VaultBasePtr;

	/** Calculate vault memory region boundaries. */
	VaultBaseAddr = (u8 *)(XAsufw_ReadReg(XASU_RTCA_KEYVAULT_BASEADDR_LOW));

	/**
	 * Compact memory with a single contiguous copy.
	 * Shift all data after the target vault down to fill the gap.
	 */
	ShiftSrcPtr = (u8 *)DelVaultBasePtr + DelVaultSize;
	ShiftSize = (u32)((VaultBaseAddr + VaultManager.TotalVaultSize - VaultManager.RemainingVaultSize) -
			ShiftSrcPtr);

	if (ShiftSize > 0U) {
		/** Copy all subsequent vault data in one operation. */
		Status = Xil_SMemCpy(DelVaultBasePtr, ShiftSize, ShiftSrcPtr, ShiftSize, ShiftSize);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_MEM_COPY_FAIL;
			goto END;
		}

		/** Update base pointers for all vaults that were shifted. */
		for (Index = 0U; Index < XKEYMANAGER_MAX_VAULTS; Index++) {
			if ((Index != VaultId) && (VaultManager.VaultInfo[Index].VaultSize != 0U)) {
				if (VaultManager.VaultInfo[Index].VaultBasePtr > DelVaultBasePtr) {
					VaultManager.VaultInfo[Index].VaultBasePtr =
						(u32 *)((u8 *)VaultManager.VaultInfo[Index].VaultBasePtr -
							DelVaultSize);
				}
			}
		}
	}

	/**
	 * Securely zeroize the freed region at the end to clear residual data.
	 * After compaction, freed space is at: (used area end) for DelVaultSize bytes.
	 */
	ResidualDataPtr = VaultBaseAddr + VaultManager.TotalVaultSize - VaultManager.RemainingVaultSize -
			DelVaultSize;

	/** Return the vault size back to available space. */
	VaultManager.RemainingVaultSize += DelVaultSize;
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = Xil_SecureZeroize(ResidualDataPtr, DelVaultSize);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
	}

	/** Clear the vault's info entry. */
	if (Xil_SMemSet(&VaultManager.VaultInfo[VaultId], sizeof(XKeyManager_VaultInfo),
				0U, sizeof(XKeyManager_VaultInfo)) != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
	}

	if (Status != XASUFW_SUCCESS) {
		XFIH_GOTO(END);
	}

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	This function deletes a single key from the vault. Only the vault owner can
 *		delete keys from the vault.
 *
 * @param	KeyId		Composite key identifier containing VaultId (bits [23:16]),
 *				KeyType (bits [31:24]), and KeyIndex (bits [15:0]).
 * @param	SubSystemId	Subsystem ID requesting the deletion (must be vault owner).
 *
 * @return
 * 	- XASUFW_SUCCESS, if key deletion is successful.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if input parameters are invalid.
 * 	- XASUFW_KEYMANAGER_VAULT_NOT_FOUND, if vault is not found.
 * 	- XASUFW_KEYMANAGER_ACCESS_DENIED, if subsystem is not the vault owner.
 * 	- XASUFW_KEYMANAGER_KEY_NOT_FOUND, if key is not found or already deleted.
 * 	- XASUFW_ZEROIZE_MEMSET_FAIL, if memory zeroization fails.
 *
 *************************************************************************************************/
s32 XKeyManager_DeleteKey(u32 KeyId, u32 SubSystemId)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	XKeyManager *KeyVaultPtr = NULL;
	u32 VaultId = XKEYMANAGER_EXTRACT_VAULTID(KeyId);
	u32 KeyType = XKEYMANAGER_EXTRACT_KEYTYPE(KeyId);
	u32 KeyIndex = XKEYMANAGER_EXTRACT_KEYINDEX(KeyId);
	u32 KeyObjectSize = 0U;
	u8 *KeyObjectPtr = NULL;
	XKeyManager_KeyMetadata *MetadataPtr = NULL;

	/** Validate composite KeyId and extracted components. */
	if ((KeyId == 0U) || (VaultId >= XKEYMANAGER_MAX_VAULTS) ||
	    (KeyType >= XKEYMANAGER_MAX_SUB_VAULTS) || (KeyIndex == 0U)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Validate vault exists. */
	if (VaultManager.VaultInfo[VaultId].VaultSize == 0U) {
		Status = XASUFW_KEYMANAGER_VAULT_NOT_FOUND;
		goto END;
	}

	/** Check ownership - only vault owner can delete keys. */
	if (VaultManager.VaultInfo[VaultId].SubSystemId != SubSystemId) {
		Status = XASUFW_KEYMANAGER_ACCESS_DENIED;
		goto END;
	}

	/** Get vault base address. */
	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;

	/** Validate key index is within sub-vault capacity. */
	if (KeyIndex > KeyVaultPtr->SubVaultHeaders[KeyType].Capacity) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Get size of key object for this key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];
	if (KeyObjectSize == 0U) {
		Status = XASUFW_KEYMANAGER_INVALID_KEY_TYPE;
		goto END;
	}

	/** Calculate pointer to key object within sub-vault. */
	KeyObjectPtr = (u8 *)KeyVaultPtr + KeyVaultPtr->SubVaultHeaders[KeyType].Offset +
			(KeyObjectSize * (KeyIndex - XKEYMANAGER_KEY_INDEX_OFFSET));

	/** Check if key exists (KeyId in metadata should match the key index). */
	MetadataPtr = (XKeyManager_KeyMetadata *)KeyObjectPtr;
	if (MetadataPtr->KeyId != KeyIndex) {
		Status = XASUFW_KEYMANAGER_KEY_NOT_FOUND;
		goto END;
	}

	/** Update sub-vault header counters based on key state. */
	if (MetadataPtr->UsageCount > 0U) {
		/** Key is active (has remaining uses or is non-depleting). */
		KeyVaultPtr->SubVaultHeaders[KeyType].ActiveKeys--;
	} else {
		/** Key was deactivated (expired). */
		KeyVaultPtr->SubVaultHeaders[KeyType].Deactivatedkeys--;
	}

	/** Securely zeroize the entire key object (metadata + content). */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = Xil_SecureZeroize(KeyObjectPtr, KeyObjectSize);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_ZEROIZE_MEMSET_FAIL;
		XFIH_GOTO(END);
	}

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Generate an ephemeral Key/IV and optionally export it via DMA.
 *
 * @param	DmaPtr		Pointer to the XAsufw_Dma instance.
 * @param	ParamsPtr	Pointer to XAsu_KeyManagerParams.
 * @param	KeyIdPtr	Pointer to the output Key ID.
 * @param	SubSystemId	Subsystem ID for which the key is being generated.
 * @param	KeyType		Type of key to be generated.
 *
 * @return
 * 	- XASUFW_SUCCESS, when the key is generated and transferred or updated in the vault.
 * 	- XASUFW_KEYMANAGER_INVALID_PARAM, if any input parameter is invalid.
 * 	- XASUFW_KEYMANAGER_INVALID_KEY_TYPE, if the command ID does not correspond to a valid key type.
 * 	- XASUFW_KEYMANAGER_VAULT_NOT_FOUND, if the vault for the subsystem is not found.
 * 	- XASUFW_RAND_GEN_ERROR, if TRNG generation fails.
 * 	- XASUFW_DMA_COPY_FAIL, if DMA transfer fails.
 *
 *************************************************************************************************/
s32 XKeyManager_GenerateKeyIv(XAsufw_Dma *DmaPtr,
			const XAsu_KeyManagerParams *ParamsPtr, u32 *KeyIdPtr, u32 SubSystemId,
			XKeyManager_SubVaultType KeyType)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	s32 ClearStatus = XASUFW_FAILURE;
	u8 EphemeralData[XASU_AES_KEY_SIZE_256BIT_IN_BYTES];
	u8 VaultId = 0U;

	/** Validate the input parameters. */
	if ((ParamsPtr == NULL) || (DmaPtr == NULL) || (KeyIdPtr == NULL) ||
	    (KeyType >= XKEYMANAGER_MAX_SUB_VAULTS)) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Validate key length parameters. */
	Status = XAsu_KmValidateKeyLength(ParamsPtr, (u8)KeyType);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Generate ephemeral Key/IV using TRNG. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = XAsufw_TrngGetRandomNumbers(EphemeralData, ParamsPtr->Length);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_RAND_GEN_ERROR;
		goto END_CLR;
	}

	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	if (ParamsPtr->KeyObjectAddr != 0U) {
		/** Copy key to user address via DMA if address is provided. */
		Status = XAsufw_DmaXfr(DmaPtr, (u64)(UINTPTR)EphemeralData,
				       ParamsPtr->KeyObjectAddr, ParamsPtr->Length, 0U);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_DMA_COPY_FAIL;
		}
	} else {
		Status = XAsu_KmValidateVaultParams(ParamsPtr);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_KEYMANAGER_INVALID_PARAM;
			goto END_CLR;
		}
		/** Store key in vault if no address is provided. */
		Status = XKeyManager_GetVaultId(SubSystemId, &VaultId);
		if (Status != XASUFW_SUCCESS) {
			Status = XASUFW_KEYMANAGER_VAULT_NOT_FOUND;
			goto END_CLR;
		}
		ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
		Status = XKeyManager_UpdateKeyVault(KeyType, ParamsPtr, EphemeralData,
						KeyIdPtr, VaultId);
	}

END_CLR:
	/* Securely zeroize ephemeral key data. */
	ClearStatus = Xil_SecureZeroize(EphemeralData, XASU_AES_KEY_SIZE_256BIT_IN_BYTES);
	if (ClearStatus != XASUFW_SUCCESS) {
		Status = XAsufw_UpdateErrorStatus(ClearStatus, XASUFW_ZEROIZE_MEMSET_FAIL);
	}
END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Look up the vault identifier corresponding to a subsystem.
 *
 * @param	SubSystemId	Subsystem identifier whose vault ID is requested.
 * @param	VaultIdPtr	Pointer updated with the matching vault ID on success.
 *
 * @return
 * 	- XASUFW_SUCCESS, when a matching vault is found.
 * 	- XASUFW_FAILURE, when the subsystem has no registered vault.
 *
 *************************************************************************************************/
s32 XKeyManager_GetVaultId(u32 SubSystemId, u8 *VaultIdPtr)
{
	s32 Status = XASUFW_FAILURE;
	u8 Index;

	if (VaultIdPtr == NULL) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	/** Search for vault matching the subsystem ID. */
	for (Index = 0U; Index < XKEYMANAGER_MAX_VAULTS; Index++) {
		if (VaultManager.VaultInfo[Index].SubSystemId == SubSystemId) {
			*VaultIdPtr = Index;
			Status = XASUFW_SUCCESS;
			break;
		}
	}

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Resolve key object from key vault if KeyId is provided.
 *
 * @param	KeyObject		Pointer to the XAsu_AesKeyObject structure.
 * @param	SubSystemId		Subsystem ID of the requested subsystem.
 * @param	KeyUsecase		Requested use case for the key.
 *
 * @return
 *		- XASUFW_SUCCESS, if key resolution is successful.
 *		- XASUFW_AES_INVALID_KEY_ADDRESS, if get key object pointer failed.
 *
 *************************************************************************************************/
s32 XKeyManager_UpdateAesKeyObjectFromVault(XAsu_AesKeyObject *KeyObject, u32 SubSystemId,
					u8 KeyUsecase)
{
	s32 Status = XASUFW_FAILURE;
	XKeyManager_AesKeyObject *KeyVaultKeyObjectPtr;

	if (KeyObject == NULL) {
		Status = XASUFW_KEYMANAGER_INVALID_PARAM;
		goto END;
	}

	KeyVaultKeyObjectPtr = (XKeyManager_AesKeyObject* )XKeyManager_GetKeyObjectPtr(KeyObject->KeyId,
									SubSystemId, KeyUsecase);
	if (KeyVaultKeyObjectPtr == NULL) {
		Status = XASUFW_AES_INVALID_KEY_ADDRESS;
		goto END;
	}
	KeyObject->KeyAddress = (u64)(UINTPTR)KeyVaultKeyObjectPtr->Content;

	KeyObject->KeySize = (((u32)KeyVaultKeyObjectPtr->Metadata.Length - XKEYMANAGER_LENGTH_AND_KEY_CONVERSION_OFFSET)
				>> XKEYMANAGER_LENGTH_AND_KEY_CONVERSION_SHIFT);

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Fetch the next available key slot within a sub-vault.
 *
 * @param	KeyVaultPtr	Pointer to the XKeyManager instance.
 * @param	SubVault	Pointer to the sub-vault where the key is stored.
 * @param	KeyIdPtr	Pointer updated with the allocated key identifier on success.
 * @param	KeyType 	Identifier of the key type being searched.
 *
 * @return
 * 	- XASUFW_SUCCESS, when a free slot is located.
 * 	- XASUFW_FAILURE, when the sub-vault has no free slots.
 *
 *************************************************************************************************/
static s32 XKeyManager_GetFreeKeySlot(const XKeyManager* KeyVaultPtr, void* SubVault, u16 *KeyIdPtr,
			XKeyManager_SubVaultType KeyType)
{
	s32 Status = XASUFW_FAILURE;
	u16 Index = 0U;
	XKeyManager_KeyMetadata *KeyObjectPtr = (XKeyManager_KeyMetadata *)SubVault;
	u32 KeyObjectSize = 0U;
	u16 SubVaultCapacity = 0U;

	/** Get object size and capacity for this key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];
	SubVaultCapacity = KeyVaultPtr->SubVaultHeaders[KeyType].Capacity;

	/** Search through sub-vault for free slot. */
	for (Index = 0U; Index < SubVaultCapacity; Index++) {
		if (KeyObjectPtr->KeyId == 0U) {
			*KeyIdPtr = (Index + 1U);
			Status = XASUFW_SUCCESS;
			break;
		}
		KeyObjectPtr = (XKeyManager_KeyMetadata *)((u8 *)KeyObjectPtr + KeyObjectSize);
	}

	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Update vault for the provided key identifier.
 *
 * @param	KeyType		Sub-vault that contains the key.
 * @param	ParamsPtr	Pointer to the XAsu_KeyManagerParams.
 * @param	KeyData		Pointer to the key data.
 * @param	KeyIdPtr	Pointer to the output Key ID.
 * @param	VaultId		Keyvault ID for which the key is being updated.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key vault updation is successful.
 * 	- XASUFW_FAILURE, if key vault updation fails.
 * 	- XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED, if the maximum capacity of the sub-vault is reached.
 * 	- XASUFW_MEM_COPY_FAIL, if memory copy operation fails.
 *
 *************************************************************************************************/
static s32 XKeyManager_UpdateKeyVault(XKeyManager_SubVaultType KeyType,
			const XAsu_KeyManagerParams *ParamsPtr, const u8 *KeyData, u32 *KeyIdPtr,
			u8 VaultId)
{
	CREATE_VOLATILE(Status, XASUFW_FAILURE);
	u32 SubVaultOffset;
	void* SubVaultBasePtr;
	XKeyManager* KeyVaultPtr;
	XKeyManager_KeyMetadata *KeyMetadataPtr;
	void* KeyContentPtr;
	u16 KeyId = 0U;
	u32 KeyObjectSize = 0U;
	u32 MaxKeySize = 0U;

	/** Get vault pointer and sub-vault offset. */
	KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;
	SubVaultOffset = KeyVaultPtr->SubVaultHeaders[KeyType].Offset;

	/** Calculate sub-vault base address. */
	SubVaultBasePtr = (u8 *)KeyVaultPtr + SubVaultOffset;

	/** Find a free key slot in the sub-vault. */
	Status = XKeyManager_GetFreeKeySlot(KeyVaultPtr, SubVaultBasePtr, &KeyId, KeyType);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED;
		goto END;
	}

	/* Determine maximum key size based on key type. */
	if (KeyType == XKEYMANAGER_AES_SUBVAULT_ID) {
		MaxKeySize = XASU_AES_KEY_SIZE_256BIT_IN_BYTES;
	} else if (KeyType == XKEYMANAGER_IV_SUBVAULT_ID) {
		MaxKeySize = XASU_AES_IV_SIZE_128BIT_IN_BYTES;
	} else {
		/* Do nothing */
	}

	/** Get key object size based on key type. */
	KeyObjectSize = XKeyManager_KeyObjectSizeLookup[KeyType];

	/** Calculate pointer to key metadata within sub-vault. */
	KeyMetadataPtr = (XKeyManager_KeyMetadata *)((u8 *)SubVaultBasePtr + (KeyObjectSize *
							((u32)KeyId - XKEYMANAGER_KEY_INDEX_OFFSET)));

	/** Calculate pointer to key content area. */
	KeyContentPtr = (u8 *)KeyMetadataPtr + sizeof(XKeyManager_KeyMetadata);

	/** Copy key data to vault. */
	ASSIGN_VOLATILE(Status, XASUFW_FAILURE);
	Status = Xil_SMemCpy(KeyContentPtr, MaxKeySize, KeyData, MaxKeySize, ParamsPtr->Length);
	if (Status != XASUFW_SUCCESS) {
		Status = XASUFW_MEM_COPY_FAIL;
		goto END;
	}

	/** Populate key metadata fields. */
	KeyMetadataPtr->AccessRights = ParamsPtr->AccessRights;
	KeyMetadataPtr->KeyType = (u8)KeyType;
	KeyMetadataPtr->EpochTime = ParamsPtr->EpochTime;
	KeyMetadataPtr->UsageCount = ParamsPtr->UsageCount;
	KeyMetadataPtr->Length = ParamsPtr->Length;
	KeyMetadataPtr->KeyUseCase = ParamsPtr->KeyUseCase;
	KeyMetadataPtr->VaultId = VaultId;
	KeyMetadataPtr->KeyId = KeyId;

	/** Increment active keys counter in sub-vault header. */
	KeyVaultPtr->SubVaultHeaders[KeyType].ActiveKeys++;

	/** Generate composite KeyId from components which consists of Key Index, VaultId and Keytype. */
	*KeyIdPtr = (u32)KeyId |
		((u32)VaultId << XKEYMANAGER_VAULT_ID_SHIFT_VAL) |
		((u32)KeyType << XKEYMANAGER_KEY_TYPE_SHIFT_VAL);

	Status = XASUFW_SUCCESS;

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Update vault information for a new vault.
 *
 * @param	SubSystemId	Subsystem identifier for the vault.
 * @param	VaultIdPtr	Pointer to the output vault ID.
 *
 * @return
 * 	- XASUFW_SUCCESS, if vault information is updated successfully.
 * 	- XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED, if no space is available for a new vault.
 * 	- XASUFW_KEYMANAGER_NEW_VAULT_NOT_ALLOWED, if a vault for the subsystem already exists.
 *
 *************************************************************************************************/
static s32 XKeyManager_UpdateVaultInfo(u32 SubSystemId, u8 *VaultIdPtr)
{
	s32 Status = XASUFW_KEYMANAGER_MAX_CAPACITY_REACHED;
	u8 Index = 0U;

	/** Check if vault already exists for this subsystem and error out if exists. */
	for (Index = 0U; Index < XKEYMANAGER_MAX_VAULTS; Index++) {
		if (VaultManager.VaultInfo[Index].SubSystemId == SubSystemId) {
			Status = XASUFW_KEYMANAGER_NEW_VAULT_NOT_ALLOWED;
			goto END;
		}
	}

	/** Find a free vault slot. */
	for (Index = 0U; Index < XKEYMANAGER_MAX_VAULTS; Index++) {
		if (VaultManager.VaultInfo[Index].VaultSize == 0U) {
			VaultManager.VaultInfo[Index].SubSystemId = SubSystemId;
			*VaultIdPtr = Index;
			Status = XASUFW_SUCCESS;
			break;
		}
	}

END:
	return Status;
}

/*************************************************************************************************/
/**
 * @brief	Validate key access rights and usage permissions for a given key.
 *
 * @param	SubSystemId	Subsystem identifier requesting key access.
 * @param	SubVaultPtr	Pointer to the sub-vault containing the key.
 * @param	KeyUseCase	Requested use case for the key.
 * @param	VaultId		Identifier of the vault containing the key.
 *
 * @return
 * 	- XASUFW_SUCCESS, if key is valid and accessible for the requested use case.
 * 	- XASUFW_KEYMANAGER_ACCESS_DENIED, if subsystem lacks access rights.
 * 	- XASUFW_KEYMANAGER_KEY_EXPIRED, if key usage count is exhausted.
 * 	- XASUFW_KEYMANAGER_KEY_USE_CASE_ERROR, if key use case is not allowed.
 *
 *************************************************************************************************/
static s32 XKeyManager_CheckKeyValidity(u32 SubSystemId, u8 *SubVaultPtr, u8 KeyUseCase, u8 VaultId)
{
	s32 Status = XASUFW_FAILURE;
	u8 Index = 0U;
	XKeyManager_KeyMetadata *MetaDataPtr = (XKeyManager_KeyMetadata *)SubVaultPtr;
	u8 AllowedAccessRights = 0U;
	XKeyManager *KeyVaultPtr = (XKeyManager *)VaultManager.VaultInfo[VaultId].VaultBasePtr;

	/** Check for access rights. */
	if (VaultManager.VaultInfo[VaultId].SubSystemId != SubSystemId) {
		AllowedAccessRights = MetaDataPtr->AccessRights;
		for (Index = 0U; Index < XASUFW_MAX_CHANNELS_SUPPORTED; Index++) {
			if (XKEYMANAGER_IS_BIT_SET(AllowedAccessRights, Index)) {
				if (SubSystemId == XAsufw_GetSubsysIdFromIpiMask(XAsufw_GetIpiMask(Index))) {
					break;
				}
			}
		}
		if (Index == XASUFW_MAX_CHANNELS_SUPPORTED) {
			Status = XASUFW_KEYMANAGER_ACCESS_DENIED;
			goto END;
		}
	}

	/** Check for key use case. */
	if ((MetaDataPtr->KeyUseCase & (XKEYMANAGER_KEYUSECASE_BITMASK(KeyUseCase))) == 0U) {
		Status = XASUFW_KEYMANAGER_KEY_USE_CASE_ERROR;
		goto END;
	}

	/*TODO: Update checking with epoch time.*/
	/** Decrement usage count if applicable. */
	if (MetaDataPtr->UsageCount != XASU_KM_USAGE_COUNT_NON_DEPLETING_VALUE) {
		if (MetaDataPtr->UsageCount == 0U) {
			Status = XASUFW_KEYMANAGER_KEY_EXPIRED;
			goto END;
		}
		MetaDataPtr->UsageCount -= XKEYMANAGER_USAGE_COUNT_DEC_VALUE;

		/** If usage count reaches zero, update counters (key transitions to deactivated). */
		if (MetaDataPtr->UsageCount == 0U) {
			KeyVaultPtr->SubVaultHeaders[MetaDataPtr->KeyType].ActiveKeys--;
			KeyVaultPtr->SubVaultHeaders[MetaDataPtr->KeyType].Deactivatedkeys++;
		}
	}

	Status = XASUFW_SUCCESS;

END:
	return Status;
}
